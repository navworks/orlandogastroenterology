				 <p>Nonalcoholic steatohepatitis or NASH is a common, often “silent” liver disease. It resembles alcoholic liver disease, but occurs in people who drink little or no alcohol. The major feature in NASH is fat in the liver, along with inflammation and damage. Most people with NASH feel well and are not aware that they have a liver problem. Nevertheless, NASH can be severe and can lead to cirrhosis, in which the liver is permanently damaged and scarred and no longer able to work properly.</p>
<p>NASH affects 2 to 5 percent of Americans. An additional 10 to 20 percent of Americans have fat in their liver, but no inflammation or liver damage, a condition called “fatty liver.” Although having fat in the liver is not normal, by itself it probably causes little harm or permanent damage. If fat is suspected based on blood test results or scans of the liver, this problem is called non-alcoholic fatty liver disease (NAFLD). If a liver biopsy is performed in this case, it will show that some people have NASH while others have simple fatty liver.</p>
<p>Both NASH and NAFLD are becoming more common, possibly because of the greater number of Americans with obesity. In the past 10 years, the rate of obesity has doubled in adults and tripled in children. Obesity also contributes to diabetes and high blood cholesterol, which can further complicate the health of someone with NASH. Diabetes and high blood cholesterol are also becoming more common among Americans.</p>
<p><strong>Diagnosis</strong></p>
<p>NASH is usually first suspected in a person who is found to have elevations in liver tests that are included in routine blood test panels, such as alanine aminotransferase (ALT) or aspartate aminotransferase (AST). When further evaluation shows no apparent reason for liver disease (such as medications, viral hepatitis, or excessive use of alcohol) and when x-rays or imaging studies of the liver show fat, NASH is suspected. The only means of proving a diagnosis of NASH and separating it from simple fatty liver is a liver biopsy. For a liver biopsy, a needle is inserted through the skin to remove a small piece of the liver. NASH is diagnosed when examination of the tissue with a microscope shows fat along with inflammation and damage to liver cells. If the tissue shows fat without inflammation and damage, simple fatty liver or NAFLD is diagnosed. An important piece of information learned from the biopsy is whether scar tissue has developed in the liver. Currently, no blood tests or scans can reliably provide this information.</p>
<p><strong>Symptoms</strong></p>
<p>NASH is usually a silent disease with few or no symptoms. Patients generally feel well in the early stages and only begin to have symptoms — such as fatigue, weight loss, and weakness — once the disease is more advanced or cirrhosis develops. The progression of NASH can take years, even decades. The process can stop and, in some cases, reverse on its own without specific therapy. Or NASH can slowly worsen, causing scarring or “fibrosis” to appear and accumulate in the liver. As fibrosis worsens, cirrhosis develops; the liver becomes seriously scarred, hardened, and unable to function normally. Not every person with NASH develops cirrhosis, but once serious scarring or cirrhosis is present, few treatments can halt the progression. A person with cirrhosis experiences fluid retention, muscle wasting, bleeding from the intestines, and liver failure. Liver transplantation is the only treatment for advanced cirrhosis with liver failure, and transplantation is increasingly performed in people with NASH. NASH ranks as one of the major causes of cirrhosis in America, behind hepatitis C and alcoholic liver disease.</p>
<p><strong>Causes</strong></p>
<p>Although NASH has become more common, its underlying cause is still not clear. It most often occurs in persons who are middle-aged and overweight or obese. Many patients with NASH have elevated blood lipids, such as cholesterol and triglycerides, and many have diabetes or pre-diabetes, but not every obese person or every patient with diabetes has NASH. Furthermore, some patients with NASH are not obese, do not have diabetes, and have normal blood cholesterol and lipids. NASH can occur without any apparent risk factor and can even occur in children. Thus, NASH is not simply obesity that affects the liver.</p>
<p>While the underlying reason for the liver injury that causes NASH is not known, several factors are possible candidates:</p>
<ul>
<li>insulin resistance</li>
<li>release of toxic inflammatory proteins by fat cells (cytokines)</li>
<li>oxidative stress (deterioration of cells) inside liver cells</li>
</ul>
<p><strong>Treatment</strong></p>
<p>Currently, no specific therapies for NASH exist. The most important recommendations given to persons with this disease are to:</p>
<ul>
<li>reduce their weight (if obese or overweight)</li>
<li>follow a balanced and healthy diet</li>
<li>increase physical activity</li>
<li>avoid alcohol</li>
<li>avoid unnecessary medications</li>
</ul>
<p>These are standard recommendations, but they can make a difference. They are also helpful for other conditions, such as heart disease, diabetes, and high cholesterol.</p>
<p>A major attempt should be made to lower body weight into the healthy range. Weight loss can improve liver tests in patients with NASH and may reverse the disease to some extent. Research at present is focusing on how much weight loss improves the liver in patients with NASH and whether this improvement lasts over a period of time. People with NASH often have other medical conditions, such as diabetes, high blood pressure, or elevated cholesterol. These conditions should be treated with medication and adequately controlled; having NASH or elevated liver enzymes should not lead people to avoid treating these other conditions.</p>
<p>Experimental approaches under evaluation in patients with NASH include antioxidants, such as vitamin E, selenium, and betaine. These medications act by reducing the oxidative stress that appears to increase inside the liver in patients with NASH. Whether these substances actually help treat the disease is not known, but the results of clinical trials should become available in the next few years.</p>
<p>Another experimental approach to treating NASH is the use of newer anti-diabetic medications — even in persons without diabetes. Most patients with NASH have insulin resistance, meaning that the insulin normally present in the bloodstream is less effective for them in controlling blood glucose and fatty acids in the blood than it is for people who do not have NASH. The newer anti-diabetic medications make the body more sensitive to insulin and may help reduce liver injury in patients with NASH. Studies of these medications — including metformin, rosiglitazone, and pioglitazone — are being sponsored by the National Institutes of Health and should answer the question of whether these medications are beneficial in NASH.</p>
<p><strong>Hope through Research</strong></p>
<p>What is most needed in the management of NASH is more research to better understand the liver injury found in this disease. When the pathways that lead to the injury are fully known, safe and effective means can be developed to reverse these pathways and help patients with NASH. Recent breakthroughs in mapping the human genome and uncovering the individual steps by which insulin and other hormones regulate blood glucose and fat could provide the necessary clues.</p>
<p>The National Institute of Diabetes and Digestive and Kidney Diseases funds the NASH Clinical Research Network, which comprises eight clinical centers located throughout the United States and a coordinating center at Johns Hopkins University. The NASH network researches the nature and underlying cause of NASH and conducts clinical studies on prevention and treatment. More information on the NASH Clinical Research Network and the locations of the clinical centers are available at <a href="http://www.jhucct.com/nash/" target="_blank">www.jhucct.com/nash/</a>.</p>
<p><strong>Points to Remember</strong></p>
<ul>
<li>Non-alcoholic steatohepatitis (NASH) is fat in the liver, with inflammation and damage.</li>
<li>NASH occurs in people who drink little or no alcohol and affects 2 to 5 percent of Americans, especially people who are middle-aged and overweight or obese.</li>
<li>NASH can occur in children.</li>
<li>People who have NASH may feel well and may not know that they have a liver disease.</li>
<li>NASH can lead to cirrhosis, a condition in which the liver is permanently damaged and cannot work properly.</li>
<li>Fatigue can occur at any stage of NASH.</li>
<li>Weight loss and weakness may begin once the disease is advanced or cirrhosis is present.</li>
<li>NASH may be suspected if blood tests show high levels of liver enzymes or if scans show fatty liver.</li>
<li>NASH is diagnosed by examining a small piece of the liver taken through a needle, a procedure called biopsy.</li>
<li>People who have NASH should reduce their weight, eat a balanced diet, engage in physical activity, and avoid alcohol and unnecessary medications.</li>
<li>No specific therapies for NASH exist. Experimental therapies being studied include antioxidants and anti-diabetes medications.</li>
</ul>
