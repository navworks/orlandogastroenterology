				 <h3 id="wha">What is ulcerative colitis?</h3>
<p>Ulcerative colitis is a disease that causes inflammation and sores, called ulcers, in the lining of the rectum and colon. Ulcers form where inflammation has killed the cells that usually line the colon, then bleed and produce pus. Inflammation in the colon also causes the colon to empty frequently, causing diarrhea.</p>
<p>When the inflammation occurs in the rectum and lower part of the colon it is called ulcerative proctitis. If the entire colon is affected it is called pancolitis. If only the left side of the colon is affected it is called limited or distal colitis.</p>
<p>Ulcerative colitis is an inflammatory bowel disease (IBD), the general name for diseases that cause inflammation in the small intestine and colon. It can be difficult to diagnose because its symptoms are similar to other intestinal disorders and to another type of IBD called Crohn’s disease. Crohn’s disease differs because it causes inflammation deeper within the intestinal wall and can occur in other parts of the digestive system including the small intestine, mouth, esophagus, and stomach.</p>
<p>Ulcerative colitis can occur in people of any age, but it usually starts between the ages of 15 and 30, and less frequently between 50 and 70 years of age. It affects men and women equally and appears to run in families, with reports of up to 20 percent of people with ulcerative colitis having a family member or relative with ulcerative colitis or Crohn’s disease. A higher incidence of ulcerative colitis is seen in Whites and people of Jewish descent.</p>
<p><strong>What are the symptoms of ulcerative colitis?</strong></p>
<p>The most common symptoms of ulcerative colitis are abdominal pain and bloody diarrhea. Patients also may experience:</p>
<ul  class="arrow-list">
<li>anemia</li>
<li>fatigue</li>
<li>weight loss</li>
<li>loss of appetite</li>
<li>rectal bleeding</li>
<li>loss of body fluids and nutrients</li>
<li>skin lesions</li>
<li>joint pain</li>
<li>growth failure (specifically in children)</li>
</ul>
<p>About half of the people diagnosed with ulcerative colitis have mild symptoms. Others suffer frequent fevers, bloody diarrhea, nausea, and severe abdominal cramps. Ulcerative colitis may also cause problems such as arthritis, inflammation of the eye, liver disease, and osteoporosis. It is not known why these problems occur outside the colon. Scientists think these complications may be the result of inflammation triggered by the immune system. Some of these problems go away when the colitis is treated.</p>
<p><strong>What causes ulcerative colitis?</strong></p>
<p>Many theories exist about what causes ulcerative colitis. People with ulcerative colitis have abnormalities of the immune system, but doctors do not know whether these abnormalities are a cause or a result of the disease. The body’s immune system is believed to react abnormally to the bacteria in the digestive tract.</p>
<p>Ulcerative colitis is not caused by emotional distress or sensitivity to certain foods or food products, but these factors may trigger symptoms in some people. The stress of living with ulcerative colitis may also contribute to a worsening of symptoms.</p>
<p><strong>How is ulcerative colitis diagnosed?</strong></p>
<p>Many tests are used to diagnose ulcerative colitis. A physical exam and medical history are usually the first step. Blood tests may be done to check for anemia, which could indicate bleeding in the colon or rectum, or they may uncover a high white blood cell count, which is a sign of inflammation somewhere in the body.</p>
<p>A stool sample can also reveal white blood cells, whose presence indicates ulcerative colitis or inflammatory disease. In addition, a stool sample allows the doctor to detect bleeding or infection in the colon or rectum caused by bacteria, a virus, or parasites.</p>
<p>A colonoscopy or sigmoidoscopy are the most accurate methods for making a diagnosis of ulcerative colitis and ruling-out other possible conditions, such as Crohn’s disease, diverticular disease, or cancer. For both tests, the doctor inserts an endoscope — a long, flexible, lighted tube connected to a computer and TV monitor — into the anus to see the inside of the colon and rectum. The doctor will be able to see any inflammation, bleeding, or ulcers on the colon wall. During the exam, the doctor may do a biopsy, which involves taking a sample of tissue from the lining of the colon to view with a microscope.</p>
<p>Sometimes x rays such as a barium enema or CT scans are also used to diagnose ulcerative colitis or its complications.</p>
<p><strong>What is the treatment for ulcerative colitis?</strong></p>
<p>Treatment for ulcerative colitis depends on the severity of the disease. Each person experiences ulcerative colitis differently, so treatment is adjusted for each individual.</p>
<p><span style="text-decoration: underline;">Drug Therapy</span></p>
<p>The goal of drug therapy is to induce and maintain remission, and to improve the quality of life for people with ulcerative colitis. Several types of drugs are available.</p>
<ul  class="arrow-list">
<li><strong>Aminosalicylates</strong>, drugs that contain 5-aminosalicyclic acid (5-ASA), help control inflammation. Sulfasalazine is a combination of sulfapyridine and 5-ASA. The sulfapyridine component carries the anti-inflammatory 5-ASA to the intestine. However, sulfapyridine may lead to side effects such as nausea, vomiting, heartburn, diarrhea, and headache. Other 5-ASA agents, such as olsalazine, mesalamine, and balsalazide, have a different carrier, fewer side effects, and may be used by people who cannot take sulfasalazine. 5-ASAs are given orally, through an enema, or in a suppository, depending on the location of the inflammation in the colon. Most people with mild or moderate ulcerative colitis are treated with this group of drugs first. This class of drugs is also used in cases of relapse.</li>
<li><strong>Corticosteroids</strong> such as prednisone, methylprednisone, and hydrocortisone also reduce inflammation. They may be used by people who have moderate to severe ulcerative colitis or who do not respond to 5-ASA drugs. Corticosteroids, also known as steroids, can be given orally, intravenously, through an enema, or in a suppository, depending on the location of the inflammation. These drugs can cause side effects such as weight gain, acne, facial hair, hypertension, diabetes, mood swings, bone mass loss, and an increased risk of infection. For this reason, they are not recommended for long-term use, although they are considered very effective when prescribed for short-term use.</li>
<li><strong>Immunomodulators</strong> such as azathioprine and 6-mercapto-purine (6-MP) reduce inflammation by affecting the immune system. These drugs are used for patients who have not responded to 5-ASAs or corticosteroids or who are dependent on corticosteroids. Immunomodulators are administered orally, however, they are slow-acting and it may take up to 6 months before the full benefit. Patients taking these drugs are monitored for complications including pancreatitis, hepatitis, a reduced white blood cell count, and an increased risk of infection. Cyclosporine A may be used with 6-MP or azathioprine to treat active, severe ulcerative colitis in people who do not respond to intravenous corticosteroids.</li>
</ul>
<p>Other drugs may be given to relax the patient or to relieve pain, diarrhea, or infection.</p>
<p>Some people have remissions — periods when the symptoms go away — that last for months or even years. However, most patients’ symptoms eventually return.</p>
<p><span style="text-decoration: underline;">Hospitalization</span></p>
<p>Occasionally, symptoms are severe enough that a person must be hospitalized. For example, a person may have severe bleeding or severe diarrhea that causes dehydration. In such cases the doctor will try to stop diarrhea and loss of blood, fluids, and mineral salts. The patient may need a special diet, feeding through a vein, medications, or sometimes surgery.</p>
<p><span style="text-decoration: underline;">Surgery</span></p>
<p>About 25 to 40 percent of ulcerative colitis patients must eventually have their colons removed because of massive bleeding, severe illness, rupture of the colon, or risk of cancer. Sometimes the doctor will recommend removing the colon if medical treatment fails or if the side effects of corticosteroids or other drugs threaten the patient’s health.</p>
<p>Surgery to remove the colon and rectum, known as proctocolectomy, is followed by one of the following:</p>
<ul  class="arrow-list">
<li>Ileostomy, in which the surgeon creates a small opening in the abdomen, called a stoma, and attaches the end of the small intestine, called the ileum, to it. Waste will travel through the small intestine and exit the body through the stoma. The stoma is about the size of a quarter and is usually located in the lower right part of the abdomen near the beltline. A pouch is worn over the opening to collect waste, and the patient empties the pouch as needed.</li>
<li>Ileoanal anastomosis, or pull-through operation, which allows the patient to have normal bowel movements because it preserves part of the anus. In this operation, the surgeon removes the colon and the inside of the rectum, leaving the outer muscles of the rectum. The surgeon then attaches the ileum to the inside of the rectum and the anus, creating a pouch. Waste is stored in the pouch and passes through the anus in the usual manner. Bowel movements may be more frequent and watery than before the procedure. Inflammation of the pouch (pouchitis) is a possible complication.</li>
</ul>
<p>Not every operation is appropriate for every person. Which surgery to have depends on the severity of the disease and the patient’s needs, expectations, and lifestyle. People faced with this decision should get as much information as possible by talking to their doctors, to nurses who work with colon surgery patients (enterostomal therapists), and to other colon surgery patients. Patient advocacy organizations can direct people to support groups and other information resources.</p>
<p><strong>Is colon cancer a concern?</strong></p>
<p>About 5 percent of people with ulcerative colitis develop colon cancer. The risk of cancer increases with the duration of the disease and how much the colon has been damaged. For example, if only the lower colon and rectum are involved, the risk of cancer is no higher than normal. However, if the entire colon is involved, the risk of cancer may be as much as 32 times the normal rate.</p>
<p>Sometimes precancerous changes occur in the cells lining the colon. These changes are called “dysplasia.” People who have dysplasia are more likely to develop cancer than those who do not. Doctors look for signs of dysplasia when doing a colonoscopy or sigmoidoscopy and when examining tissue removed during these tests.</p>
<p>According to the 2002 updated guidelines for colon cancer screening, people who have had IBD throughout their colon for at least 8 years and those who have had IBD in only the left colon for 12 to 15 years should have a colonoscopy with biopsies every 1 to 2 years to check for dysplasia. Such screening has not been proven to reduce the risk of colon cancer, but it may help identify cancer early. These guidelines were produced by an independent expert panel and endorsed by numerous organizations, including the American Cancer Society, the American College of Gastroenterology, the American Society of Colon and Rectal Surgeons, and the Crohn’s &amp; Colitis Foundation of America.</p>
