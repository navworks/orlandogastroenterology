				 <h3 id="wha">What is diarrhea?</h3>

<p>Diarrhea is loose, watery stools. A person with diarrhea typically passes stool more than three times a day. People with diarrhea may pass more than a quart of stool a day. Acute diarrhea is a common problem that usually lasts 1 or 2 days and goes away on its own without special treatment. Prolonged diarrhea persisting for more than 2 days may be a sign of a more serious problem and poses the risk of dehydration. Chronic diarrhea may be a feature of a chronic disease.</p>
<p>Diarrhea can cause dehydration, which means the body lacks enough fluid to function properly. Dehydration is particularly dangerous in children and older people, and it must be treated promptly to avoid serious health problems. See Dehydration.</p>
<p>People of all ages can get diarrhea and the average adult has a bout of acute diarrhea about four times a year. In the United States, each child will have had seven to 15 episodes of diarrhea by age 5.</p>
<p><strong>What causes diarrhea?</strong></p>
<p>Acute diarrhea is usually related to a bacterial, viral, or parasitic infection. Chronic diarrhea is usually related to functional disorders such as irritable bowel syndrome or inflammatory bowel disease.</p>
<p>A few of the more common causes of diarrhea include the following:</p>
<ul>
<li><strong>Bacterial infections</strong> – Several types of bacteria consumed through contaminated food or water can cause diarrhea. Common culprits include Campylobacter, Salmonella, Shigella, and Escherichia coli (E. coli).</li>
<li><strong>Viral infections</strong> – Many viruses cause diarrhea, including rotavirus, Norwalk virus, cytomegalovirus, herpes simplex virus, and viral hepatitis.</li>
<li><strong>Food intolerances</strong> – Some people are unable to digest food components such as artificial sweeteners and lactose — the sugar found in milk.</li>
<li><strong>Parasites</strong> – Parasites can enter the body through food or water and settle in the digestive system. Parasites that cause diarrhea include Giardia lamblia, Entamoeba histolytica, and Cryptosporidium.</li>
<li><strong>Reaction to medicines</strong> – Antibiotics, blood pressure medications, cancer drugs, and antacids containing magnesium can all cause diarrhea.</li>
<li><strong>Intestinal diseases</strong> – Inflammatory bowel disease, colitis, Crohn’s disease, and celiac disease often lead to diarrhea.</li>
<li><strong>Functional bowel disorders</strong> – Diarrhea can be a symptom of irritable bowel syndrome.</li>
</ul>
<p>Some people develop diarrhea after stomach surgery or removal of the gallbladder. The reason may be a change in how quickly food moves through the digestive system after stomach surgery or an increase in bile in the colon after gallbladder surgery.</p>
<p>People who visit foreign countries are at risk for traveler’s diarrhea, which is caused by eating food or drinking water contaminated with bacteria, viruses, or parasites. Traveler’s diarrhea can be a problem for people visiting developing countries. Visitors to the United States, Canada, most European countries, Japan, Australia, and New Zealand do not face much risk for traveler’s diarrhea.</p>
<p>In many cases, the cause of diarrhea cannot be found. As long as diarrhea goes away on its own, an extensive search for the cause is not usually necessary.</p>
<p><strong>What are the symptoms of diarrhea?</strong></p>
<p>Diarrhea may be accompanied by cramping, abdominal pain, bloating, nausea, or an urgent need to use the bathroom. Depending on the cause, a person may have a fever or bloody stools.</p>
<p><strong>Diarrhea in Children</strong></p>
<p>Children can have acute and chronic forms of diarrhea. Causes include bacteria, viruses, parasites, medications, functional bowel disorders, and food sensitivities. Infection with the rotavirus is the most common cause of acute childhood diarrhea. Rotavirus diarrhea usually resolves in 3 to 9 days. Children who are 6 to 32 weeks old can be vaccinated against the virus with a vaccine called Rotateq.</p>
<p>If your child has diarrhea, do not hesitate to call the doctor for advice. Diarrhea is especially dangerous in newborns and infants, leading to dehydration in just a day or two. A child can die from dehydration within a few days. The main treatment for diarrhea in children is re-hydration to replace lost fluid quickly.</p>
<p>Take your child to the doctor if there is no improvement after 24 hours or if any of the following symptoms appear:</p>
<ul>
<li>stools containing blood or pus</li>
<li>black stools</li>
<li>a temperature above 102 degrees</li>
<li>signs of dehydration</li>
</ul>
<p>Medications to treat diarrhea in adults can be dangerous for children and should only be given with a doctor’s guidance.</p>
<p><strong>Dehydration</strong></p>
<p>Diarrhea can cause dehydration, which means the body has lost too much fluid and too many electrolytes and can’t function properly. Dehydration is particularly dangerous in children and in older adults and must be treated promptly to avoid serious health problems.</p>
<p>Signs of dehydration include:</p>
<ul>
<li>thirst</li>
<li>less frequent urination</li>
<li>dry skin</li>
<li>fatigue</li>
<li>light-headedness</li>
<li>dark-colored urine</li>
</ul>
<p>Signs of dehydration in children include:</p>
<ul>
<li>dry mouth and tongue</li>
<li>no tears when crying</li>
<li>no wet diapers for 3 hours or more</li>
<li>sunken abdomen, eyes, or cheeks</li>
<li>high fever</li>
<li>listlessness or irritability</li>
<li>skin that does not flatten when pinched and released</li>
</ul>
<p>If you suspect that you or your child is dehydrated, call the doctor immediately. Severe dehydration may require hospitalization.</p>
<p><strong>Preventing Dehydration</strong></p>
<p>The fluid and electrolytes lost during diarrhea need to be replaced promptly because the body cannot function without them. Electrolytes are the salts and minerals that affect the amount of water in your body, muscle activity, and other important functions.</p>
<p>Although water is extremely important in preventing dehydration, it does not contain electrolytes. Broth and soups that contain sodium, and fruit juices, soft fruits, or vegetables that contain potassium, help restore electrolyte levels. Over-the-counter rehydration solutions such as Pedialyte, Ceralyte, and Infalyte are also good electrolyte sources and are especially recommended for use in children.</p>
<p><strong>When should a doctor be consulted?</strong></p>
<p>Diarrhea is not usually harmful, but it can become dangerous or signal a more serious problem. You should see the doctor if you experience any of the following:</p>
<ul>
<li>diarrhea for more than 3 days</li>
<li>severe pain in the abdomen or rectum</li>
<li>a fever of 102 degrees or higher</li>
<li>blood in your stool or black, tarry stools</li>
<li>signs of dehydration</li>
</ul>
<p>How is the cause of diarrhea diagnosed?</p>
<p><strong>Diagnostic tests to find the cause of diarrhea may include the following:</strong></p>
<ul>
<ul>
<li><strong>Medical history and physical examination</strong> – The doctor will ask you about your eating habits and medication use and will examine you for signs of illness.</li>
<li><strong>Stool culture</strong> – A sample of stool is analyzed in a laboratory to check for bacteria, parasites, or other signs of disease and infection.</li>
<li><strong>Blood tests</strong> – Blood tests can be helpful in ruling out certain diseases.</li>
<li><strong>Fasting tests</strong> – To find out if a food intolerance or allergy is causing the diarrhea, the doctor may ask you to avoid lactose, carbohydrates, wheat, or other foods to see whether the diarrhea responds to a change in diet.</li>
<li><strong>Sigmoidoscopy</strong> – For this test, the doctor uses a special instrument to look at the inside of the rectum and lower part of the colon.</li>
<li><strong>Colonoscopy</strong> – This test is similar to a sigmoidoscopy, but it allows the doctor to view the entire colon.</li>
<li><strong>Imaging tests</strong> – These tests can rule out structural abnormalities as the cause of diarrhea.</li>
</ul>
</ul>
<p><strong>How is diarrhea treated?</strong></p>
<p>In most cases of diarrhea, replacing lost fluid to prevent dehydration is the only treatment necessary. Medicines that stop diarrhea may be helpful, but they are not recommended for people whose diarrhea is caused by a bacterial infection or parasite. If you stop the diarrhea before having purged the bacteria or parasite, you will trap the organism in the intestines and prolong the problem. Rather, doctors usually prescribe antibiotics as a first-line treatment. Viral infections are either treated with medication or left to run their course, depending on the severity and type of virus.</p>
<p><span style="text-decoration: underline;">Tips About Food</span></p>
<p>Until diarrhea subsides, try to avoid caffeine, milk products, and foods that are greasy, high in fiber, or very sweet. These foods tend to aggravate diarrhea.</p>
<p>As you improve, you can add soft, bland foods to your diet, including bananas, plain rice, boiled potatoes, toast, crackers, cooked carrots, and baked chicken without the skin or fat. For children, the pediatrician may also recommend a bland diet. Once the diarrhea has stopped, the pediatrician will likely encourage children to return to a normal and healthy diet if it can be tolerated.</p>
<p><strong>Preventing Traveler’s Diarrhea</strong></p>
<p>Traveler’s diarrhea happens when you consume food or water contaminated with bacteria, viruses, or parasites. You can take the following precautions to prevent traveler’s diarrhea when you travel outside of the United States:</p>
<ul>
<li>Do not drink tap water or use it to brush your teeth.</li>
<li>Do not drink unpasteurized milk or dairy products.</li>
<li>Do not use ice made from tap water.</li>
<li>Avoid all raw fruits and vegetables, including lettuce and fruit salads, unless they can be peeled and you peel them yourself.</li>
<li>Do not eat raw or rare meat and fish.</li>
<li>Do not eat meat or shellfish that is not hot when served.</li>
<li>Do not eat food from street vendors.</li>
</ul>
<p>You can safely drink bottled water — if you are the one to break the seal — along with carbonated soft drinks, and hot drinks such as coffee or tea.</p>
<p>Depending on where you are going and how long you will stay, your doctor may recommend that you take antibiotics before leaving to protect you from possible infection.</p>
<p><strong>Points to Remember</strong></p>
<ul>
<li>Diarrhea is a common problem that usually resolves on its own.</li>
<li>Diarrhea is dangerous if a person becomes dehydrated.</li>
<li>Causes include viral, bacterial, parasitic infections, food intolerance, reactions to medicine, intestinal diseases, and functional bowel disorders.</li>
<li>Treatment involves replacing lost fluid and electrolytes. Depending on the cause of the problem, a person might also need medication to stop the diarrhea or treat an infection. Children may need an oral rehydration solution to replace lost fluid and electrolytes.</li>
<li>Call the doctor if the person with diarrhea has severe pain in the abdomen or rectum, a fever of 102 degrees or higher, blood in the stool, signs of dehydration, or diarrhea for more than 3 days.</li>
</ul>
