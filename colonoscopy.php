				<h4>How is colonoscopy performed?</h4>
				<h5>Examination Large of the Intestine:</h5>
				<p>
					During colonoscopy, patients lie on their left side on an examination table. In most cases, a light sedative, and possibly pain medication, helps keep patients relaxed. Deeper sedation may be required in some cases. The doctor and medical staff monitor vital signs and attempt to make patients as comfortable as possible.
				</p>
				<p>During colonoscopy, patients lie on their left side on an examination table.</p>
				<p>The doctor inserts a long, flexible, lighted tube called a colonoscope, or scope, into the anus and slowly guides it through the rectum and into the colon. The scope inflates the large intestine with carbon dioxide gas to give the doctor a better view. A small camera mounted on the scope transmits a video image from inside the large intestine to a computer screen, allowing the doctor to carefully examine the intestinal lining. The doctor may ask the patient to move periodically so the scope can be adjusted for better viewing.</p>
				<p>Once the scope has reached the opening to the small intestine, it is slowly withdrawn and the lining of the large intestine is carefully examined again. Bleeding and puncture of the large intestine are possible but uncommon complications of colonoscopy.</p>
				<h5>Removal of Polyps and Biopsy:</h5>
				<p>A doctor can remove growths, called polyps, during colonoscopy and later test them in a laboratory for signs of cancer. Polyps are common in adults and are usually harmless. However, most colorectal cancer begins as a polyp, so removing polyps early is an effective way to prevent cancer.</p>
				<p>The doctor can also take samples from abnormal-looking tissues during colonoscopy. The procedure, called a biopsy, allows the doctor to later look at the tissue with a microscope for signs of disease.</p>
				<p>The doctor removes polyps and takes biopsy tissue using tiny tools passed through the scope. If bleeding occurs, the doctor can usually stop it with an electrical probe or special medications passed through the scope. Tissue removal and the treatments to stop bleeding are usually painless.</p>
