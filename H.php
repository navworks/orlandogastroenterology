<li>obstruction — when the ulcer blocks the path of food trying to leave the stomach</li>

<h4>How is an H. pylori-related ulcer diagnosed?</h4>

<P>Diagnosing an Ulcer

<P>To see whether symptoms are caused by an ulcer, the doctor may do an upper gastrointestinal (GI) series or an endoscopy. An upper GI series is an x ray of the esophagus, stomach, and duodenum. The patient drinks a chalky liquid called barium to make these organs and any ulcers show up more clearly on the x ray.</p>

<P>An endoscopy is an exam that uses an endoscope, a thin, lighted tube with a tiny camera on the end. The patient is lightly sedated, and the doctor carefully eases the endoscope into the mouth and down the throat to the stomach and duodenum. This allows the doctor to see the lining of the esophagus, stomach, and duodenum. The doctor can use the endoscope to take photos of ulcers or remove a tiny piece of tissue to view under a microscope. This procedure is called a biopsy. If an ulcer is bleeding, the doctor can use the endoscope to inject drugs that promote clotting or to guide a heat probe that cauterizes the ulcer.</p>

<P>Diagnosing H. pylori

<P>If an ulcer is found, the doctor will test the patient for H. pylori. This test is important because treatment for an ulcer caused by H. pylori is different from that for an ulcer caused by NSAIDs.</p>

<P>H. pylori is diagnosed through blood, breath, stool, and tissue tests. Blood tests are most common. They detect antibodies to H. pylori bacteria. Blood is taken at the doctor’s office through a finger stick.</p>

<P>Urea breath tests are an effective diagnostic method for H. pylori. They are also used after treatment to see whether it worked. In the doctor’s office, the patient drinks a urea solution that contains a special carbon atom. If H. pylori is present, it breaks down the urea, releasing the carbon. The blood carries the carbon to the lungs, where the patient exhales it. The breath test is 96 percent to 98 percent accurate.</p>

<P>Stool tests may be used to detect H. pylori infection in the patient’s fecal matter. Studies have shown that this test, called the Helicobacter pylori stool antigen (HpSA) test, is accurate for diagnosing H. pylori.</p>

<P>Tissue tests are usually done using the biopsy sample that is removed with the endoscope. There are three types:</p>

<li>The rapid urease test detects the enzyme urease, which is produced by H. pylori.</li>
<li>A histology test allows the doctor to find and examine the actual bacteria.</li>
<li>A culture test involves allowing H. pylori to grow in the tissue sample.</li>

<P>n diagnosing H. pylori, blood, breath, and stool tests are often done before tissue tests because they are less invasive. However, blood tests are not used to detect H. pylori following treatment because a patient’s blood can show positive results even after H. pylori has been eliminated.<P>

How are H. pylori peptic ulcers treated?

<P>H. pylori peptic ulcers are treated with drugs that kill the bacteria, reduce stomach acid, and protect the stomach lining. Antibiotics are used to kill the bacteria. Two types of acid-suppressing drugs might be used: H2 blockers and proton pump inhibitors.<P>

<P>H2 blockers work by blocking histamine, which stimulates acid secretion. They help reduce ulcer pain after a few weeks. Proton pump inhibitors suppress acid production by halting the mechanism that pumps the acid into the stomach. H2 blockers and proton pump inhibitors have been prescribed alone for years as treatments for ulcers. But used alone, these drugs do not eradicate H. pylori and therefore do not cure H. pylori-related ulcers. Bismuth subsalicylate, a component of Pepto-Bismol, is used to protect the stomach lining from acid. It also kills H. pylori. Treatment usually involves a combination of antibiotics, acid suppressors, and stomach protectors. Antibiotic regimens recommended for patients may differ across regions of the world because different areas have begun to show resistance to particular antibiotics.<P>

<P>The use of only one medication to treat H. pylori is not recommended. At this time, the most proven effective treatment is a 2-week course of treatment called triple therapy. It involves taking two antibiotics to kill the bacteria and either an acid suppressor or stomach-lining shield. Two-week triple therapy reduces ulcer symptoms, kills the bacteria, and prevents ulcer recurrence in more than 90 percent of patients.<P>

<P>Unfortunately, patients may find triple therapy complicated because it involves taking as many as 20 pills a day. Also, the antibiotics used in triple therapy may cause mild side effects such as nausea, vomiting, diarrhea, dark stools, metallic taste in the mouth, dizziness, headache, and yeast infections in women — most side effects can be treated with medication withdrawal. Nevertheless, recent studies show that 2 weeks of triple therapy is ideal. Early results of studies in other countries suggest that 1 week of triple therapy may be as effective as the 2-week therapy, with fewer side effects.<P>

<P>Another option is 2 weeks of dual therapy. Dual therapy involves two drugs: an antibiotic and an acid suppressor. It is not as effective as triple therapy.<P>

<P>Two weeks of quadruple therapy, which uses two antibiotics, an acid suppressor, and a stomach-lining shield, looks promising in research studies. It is also called bismuth triple therapy.<P>

<h4>Drugs Used to Treat H. pylori Peptic Ulcers</h4>

<h4>Antibiotics:</h4> <p>metronidazole, tetracycline, clarithromycin, amoxicillin</p>

<h4>H2 blockers:</h4> <p>cimetidine, ranitidine, famotidine, nizatidine</p>

<h4>Proton pump inhibitors:</h4> <p>omeprazole, lansoprazole, rabeprazole, esomeprazole, pantoprozole</p>

<h4>tomach-lining protector:</h4> <p>bismuth subsalicylate</p>

<h4>Can H. pylori infection be prevented?</h4>

<p>No one knows for sure how H. pylori spreads, so prevention is difficult. Researchers are trying to develop a vaccine to prevent infection.</P>

<h4>Why don’t all doctors automatically check for H. pylori?</H4>

<p>Changing medical belief and practice takes time. For nearly 100 years, scientists and doctors thought that ulcers were caused by stress, spicy food, and alcohol. Treatment involved bed rest and a bland diet. Later, researchers added stomach acid to the list of causes and began treating ulcers with antacids.</p>

<p>Since H. pylori was discovered in 1982, studies conducted around the world have shown that using antibiotics to destroy H. pylori cures peptic ulcers. The prevalence of H. pylori ulcers is changing. The infection is becoming less common in people born in developed countries. The medical community, however, continues to debate H. pylori‘s role in peptic ulcers. If you have a peptic ulcer and have not been tested for H. pylori infection, talk to your doctor.</p>

<h4>Points to Remember</h4>

<li>A peptic ulcer is a sore in the lining of the stomach or duodenum.</li>
<li>The majority of peptic ulcers are caused by the H. pylori bacterium. Many of the other cases are caused by NSAIDs. None are caused by spicy food or stress.</li>
<li>H. pylori can be transmitted from person to person through close contact and exposure to vomit.</li>
<li>Always wash your hands after using the bathroom and before eating.</li>
<li>A combination of antibiotics and other drugs is the most effective treatment for H. pylori peptic ulcers.</li>
