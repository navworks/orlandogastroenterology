				  <h4>What is a peptic ulcer?</h4>
				  <p>A peptic ulcer is a sore on the lining of the stomach or duodenum, which is the beginning of the small intestine. Peptic ulcers are common: One in 10 Americans develops an ulcer at some time in his or her life. One cause of peptic ulcer is bacterial infection, but some ulcers are caused by long-term use of nonsteroidal anti-inflammatory agents (NSAIDs), like aspirin and ibuprofen. In a few cases, cancerous tumors in the stomach or pancreas can cause ulcers. Peptic ulcers are not caused by stress or eating spicy food, but these can make ulcers worse.<p>Most of the time, gas in the body is odorless. The odor of passed gas comes from sulfur made by bacteria in the large intestine. Sometimes gas causes bloating and pain. Not everyone has these symptoms. How much gas the body makes and how sensitive a person is to gas in the large intestine have an effect on how uncomfortable one feels.</p>
				<h4>What is H. pylori?</h4>
				<p>Helicobacter pylori (H. pylori) is a type of bacteria. Researchers believe that H. pylori is responsible for the majority of peptic ulcers.

H. pylori infection is common in the United States. About 20 percent of people under 40 years old and half of those over 60 years have it. Most infected people, however, do not develop ulcers. Why H. pylori does not cause ulcers in every infected person is not known. Most likely, infection depends on characteristics of the infected person, the type of H. pylori, and other factors yet to be discovered.

Researchers are not certain how people contract H. pylori, but they think it may be through food or water. Researchers have found H. pylori in the saliva of some infected people, so the bacteria may also spread through mouth-to-mouth contact such as kissing.<p>1. Cut down on foods that cause gas.</p>
				<h4>How does H. pylori cause a peptic ulcer?</h4>

<p>H. pylori weakens the protective mucous coating of the stomach and duodenum, which allows acid to get through to the sensitive lining beneath. Both the acid and the bacteria irritate the lining and cause a sore, or ulcer.</p>

<p>H. pylori is able to survive in stomach acid because it secretes enzymes that neutralize the acid. This mechanism allows H. pylori to make its way to the “safe” area — the protective mucous lining. Once there, the bacterium’s spiral shape helps it burrow through the lining.</p>

<h4>What are the symptoms of an ulcer?</H4>

<p>Abdominal discomfort is the most common symptom. This discomfort usually:</p>
				<ul  class="arrow-list">
				<li>s a dull, gnawing ache</li>
				<li>comes and goes for several days or weeks</li>
				<li>occurs 2 to 3 hours after a meal</li>
				<li>occurs in the middle of the night — when the stomach is empty</li>
				<li>is relieved by eating</li>
				<li>is relieved by antacid medications</li>
				
