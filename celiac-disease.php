				 <h3 id="wha">What is celiac disease?</h3>

<p>Celiac disease is a digestive disease that damages the small intestine and interferes with absorption of nutrients from food. People who have celiac disease cannot tolerate gluten, a protein in wheat, rye, and barley. Gluten is found mainly in foods but may also be found in everyday products such as medicines, vitamins, and lip balms.</p>
<p>When people with celiac disease eat foods or use products containing gluten, their immune system responds by damaging or destroying villi — the tiny, fingerlike protrusions lining the small intestine. Villi normally allow nutrients from food to be absorbed through the walls of the small intestine into the bloodstream. Without healthy villi, a person becomes malnourished, no matter how much food one eats.</p>
<p>Celiac disease is both a disease of malabsorption — meaning nutrients are not absorbed properly — and an abnormal immune reaction to gluten. Celiac disease is also known as celiac sprue, nontropical sprue, and gluten-sensitive enteropathy. Celiac disease is genetic, meaning it runs in families. Sometimes the disease is triggered — or becomes active for the first time — after surgery, pregnancy, childbirth, viral infection, or severe emotional stress.</p>
<p><strong>What are the symptoms of celiac disease?</strong></p>
<p>Symptoms of celiac disease vary from person to person. Symptoms may occur in the digestive system or in other parts of the body. Digestive symptoms are more common in infants and young children and may include:</p>
<ul  class="arrow-list">
<li>abdominal bloating and pain</li>
<li>chronic diarrhea</li>
<li>vomiting</li>
<li>constipation</li>
<li>pale, foul-smelling, or fatty stool</li>
<li>weight loss</li>
</ul>
<p>Irritability is another common symptom in children. Malabsorption of nutrients during the years when nutrition is critical to a child’s normal growth and development can result in other problems such as failure to thrive in infants, delayed growth and short stature, delayed puberty, and dental enamel defects of the permanent teeth.</p>
<p>Adults are less likely to have digestive symptoms and may instead have one or more of the following:</p>
<ul  class="arrow-list">
<li>unexplained iron-deficiency anemia</li>
<li>fatigue</li>
<li>bone or joint pain</li>
<li>arthritis</li>
<li>bone loss or osteoporosis</li>
<li>depression or anxiety</li>
<li>tingling numbness in the hands and feet</li>
<li>seizures</li>
<li>missed menstrual periods</li>
<li>infertility or recurrent miscarriage</li>
<li>canker sores inside the mouth</li>
<li>an itchy skin rash called dermatitis herpetiformis</li>
</ul>
<p>People with celiac disease may have no symptoms but can still develop complications of the disease over time. Long-term complications include malnutrition — which can lead to anemia, osteoporosis, and miscarriage, among other problems — liver diseases, and cancers of the intestine.</p>
<p><strong>Why are celiac disease symptoms so varied?</strong></p>
<p>Researchers are studying the reasons celiac disease affects people differently. The length of time a person was breastfed, the age a person started eating gluten-containing foods, and the amount of gluten-containing foods one eats are three factors thought to play a role in when and how celiac disease appears. Some studies have shown, for example, that the longer a person was breastfed, the later the symptoms of celiac disease appear.</p>
<p>Symptoms also vary depending on a person’s age and the degree of damage to the small intestine. Many adults have the disease for a decade or more before they are diagnosed. The longer a person goes undiagnosed and untreated, the greater the chance of developing long-term complications.</p>
<p><strong>What other health problems do people with celiac disease have?</strong></p>
<p>People with celiac disease tend to have other diseases in which the immune system attacks the body’s healthy cells and tissues. The connection between celiac disease and these diseases may be genetic. They include:</p>
<ul  class="arrow-list">
<li>type 1 diabetes</li>
<li>autoimmune thyroid disease</li>
<li>autoimmune liver disease</li>
<li>rheumatoid arthritis</li>
<li>Addison’s disease, a condition in which the glands that produce critical hormones are damaged</li>
<li>Sjögren’s syndrome, a condition in which the glands that produce tears and saliva are destroyed</li>
</ul>
<p><strong>How common is celiac disease?</strong></p>
<p>Celiac disease affects people in all parts of the world. Originally thought to be a rare childhood syndrome, celiac disease is now known to be a common genetic disorder. More than 2 million people in the United States have the disease, or about 1 in 133 people.1 Among people who have a first-degree relative — a parent, sibling, or child — diagnosed with celiac disease, as many as 1 in 22 people may have the disease.2</p>
<p>Celiac disease is also more common among people with other genetic disorders including Down syndrome and Turner syndrome, a condition that affects girls’ development.</p>
<p><strong>How is celiac disease diagnosed?</strong></p>
<p>Recognizing celiac disease can be difficult because some of its symptoms are similar to those of other diseases. Celiac disease can be confused with irritable bowel syndrome, iron-deficiency anemia caused by menstrual blood loss, inflammatory bowel disease, diverticulitis, intestinal infections, and chronic fatigue syndrome. As a result, celiac disease has long been under diagnosed or misdiagnosed. As doctors become more aware of the many varied symptoms of the disease and reliable blood tests become more available, diagnosis rates are increasing.</p>
<p><span style="text-decoration: underline;">Blood Tests</span></p>
<p>People with celiac disease have higher than normal levels of certain auto antibodies — proteins that react against the body’s own cells or tissues — in their blood. To diagnose celiac disease, doctors will test blood for high levels of anti-tissue transglutaminase antibodies (tTGA) or anti-endomysium antibodies (EMA). If test results are negative but celiac disease is still suspected, additional blood tests may be needed.</p>
<p>Before being tested, one should continue to eat a diet that includes foods with gluten, such as breads and pastas. If a person stops eating foods with gluten before being tested, the results may be negative for celiac disease even if the disease is present.</p>
<p><span style="text-decoration: underline;">Intestinal Biopsy</span></p>
<p>If blood tests and symptoms suggest celiac disease, a biopsy of the small intestine is performed to confirm the diagnosis. During the biopsy, the doctor removes tiny pieces of tissue from the small intestine to check for damage to the villi. To obtain the tissue sample, the doctor eases a long, thin tube called an endoscope through the patient’s mouth and stomach into the small intestine. The doctor then takes the samples using instruments passed through the endoscope.</p>
<p><span style="text-decoration: underline;">Dermatitis Herpetiformis</span></p>
<p>Dermatitis herpetiformis (DH) is an intensely itchy, blistering skin rash that affects 15 to 25 percent of people with celiac disease.3 The rash usually occurs on the elbows, knees, and buttocks. Most people with DH have no digestive symptoms of celiac disease.</p>
<p>DH is diagnosed through blood tests and a skin biopsy. If the antibody tests are positive and the skin biopsy has the typical findings of DH, patients do not need to have an intestinal biopsy. Both the skin disease and the intestinal disease respond to a gluten-free diet and recur if gluten is added back into the diet. The rash symptoms can be controlled with antibiotics such as dapsone. Because dapsone does not treat the intestinal condition, people with DH must maintain a gluten-free diet.</p>
<p><span style="text-decoration: underline;">Screening</span></p>
<p>Screening for celiac disease means testing for the presence of autoantibodies in the blood in people without symptoms. Americans are not routinely screened for celiac disease. However, because celiac disease is hereditary, family members of a person with the disease may wish to be tested. Four to 12 percent of an affected person’s first-degree relatives will also have the disease.</p>
<p><strong>How is celiac disease treated?</strong></p>
<p>The only treatment for celiac disease is a gluten-free diet. Doctors may ask a newly diagnosed person to work with a dietitian on a gluten-free diet plan. A dietitian is a health care professional who specializes in food and nutrition. Someone with celiac disease can learn from a dietitian how to read ingredient lists and identify foods that contain gluten in order to make informed decisions at the grocery store and when eating out.</p>
<p>For most people, following this diet will stop symptoms, heal existing intestinal damage, and prevent further damage. Improvement begins within days of starting the diet. The small intestine usually heals in 3 to 6 months in children but may take several years in adults. A healed intestine means a person now has villi that can absorb nutrients from food into the bloodstream.</p>
<p>To stay well, people with celiac disease must avoid gluten for the rest of their lives. Eating even a small amount of gluten can damage the small intestine. The damage will occur in anyone with the disease, including people without noticeable symptoms. Depending on a person’s age at diagnosis, some problems will not improve, such as short stature and dental enamel defects.</p>
<p>Some people with celiac disease show no improvement on the gluten-free diet. The most common reason for poor response to the diet is that small amounts of gluten are still being consumed. Hidden sources of gluten include additives such as modified food starch, preservatives, and stabilizers made with wheat. And because many corn and rice products are produced in factories that also manufacture wheat products, they can be contaminated with wheat gluten.</p>
<p>Rarely, the intestinal injury will continue despite a strictly gluten-free diet. People with this condition, known as refractory celiac disease, have severely damaged intestines that cannot heal. Because their intestines are not absorbing enough nutrients, they may need to receive nutrients directly into their bloodstream through a vein, or intravenously. Researchers are evaluating drug treatments for refractory celiac disease.</p>
<p><span style="text-decoration: underline;">The Gluten-free Diet</span></p>
<p>A gluten-free diet means not eating foods that contain wheat, rye, and barley. The foods and products made from these grains should also be avoided. In other words, a person with celiac disease should not eat most grain, pasta, cereal, and many processed foods.</p>
<p>Despite these restrictions, people with celiac disease can eat a well-balanced diet with a variety of foods. They can use potato, rice, soy, amaranth, quinoa, buckwheat, or bean flour instead of wheat flour. They can buy gluten-free bread, pasta, and other products from stores that carry organic foods, or order products from special food companies. Gluten-free products are increasingly available from mainstream stores.</p>
<p>“Plain” meat, fish, rice, fruits, and vegetables do not contain gluten, so people with celiac disease can freely eat these foods. In the past, people with celiac disease were advised not to eat oats. New evidence suggests that most people can safely eat small amounts of oats, as long as the oats are not contaminated with wheat gluten during processing. People with celiac disease should work closely with their health care team when deciding whether to include oats in their diet. Examples of other foods that are safe to eat and those that are not are provided in the table.</p>
<p>The gluten-free diet requires a completely new approach to eating. Newly diagnosed people and their families may find support groups helpful as they learn to adjust to a new way of life. People with celiac disease must be cautious about what they buy for lunch at school or work, what they purchase at the grocery store, what they eat at restaurants or parties, and what they grab for a snack. Eating out can be a challenge. When in doubt about a menu item, a person with celiac disease should ask the waiter or chef about ingredients and preparation or if a gluten-free menu is available.</p>
<p>Gluten is also used in some medications. People with celiac disease should ask a pharmacist if prescribed medications contain wheat. Because gluten is sometimes used as an additive in unexpected products — such as lipstick and play dough — reading product labels is important. If the ingredients are not listed on the label, the manufacturer should provide a list upon request. With practice, screening for gluten becomes second nature.</p>
<p><span style="text-decoration: underline;">New Food Labeling</span></p>
<p>The Food Allergen Labeling and Consumer Protection Act (FALCPA), which took effect on January 1, 2006, requires food labels to clearly identify wheat and other common food allergens in the list of ingredients. FALCPA also requires the U.S. Food and Drug Administration to develop and finalize rules for the use of the term “gluten free” on product labels.</p>
