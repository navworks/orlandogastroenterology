<p><strong>Points to Remember</strong></p>
<ul  class="arrow-list">
<li>People with celiac disease cannot tolerate gluten, a protein in wheat, rye, and barley.</li>
<li>Untreated celiac disease damages the small intestine and interferes with nutrient absorption.</li>
<li>Without treatment, people with celiac disease can develop complications such as osteoporosis, anemia, and cancer.</li>
<li>A person with celiac disease may or may not have symptoms.</li>
<li>Diagnosis involves blood tests and, in most cases, a biopsy of the small intestine.</li>
<li>Since celiac disease is hereditary, family members of a person with celiac disease may wish to be tested.</li>
<li>Celiac disease is treated by eliminating all gluten from the diet. The gluten-free diet is a lifetime requirement.</li>
<li>A dietitian can teach a person with celiac disease about food selection, label reading, and other strategies to help manage the disease.</li>
</ul>
