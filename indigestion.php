				 <h3 id="wha">What is indigestion?</h3>


				 
				 <p>Indigestion, also known as dyspepsia, is a term used to describe one or more symptoms including a feeling of fullness during a meal, uncomfortable fullness after a meal, and burning or pain in the upper abdomen.</p>
<p>Indigestion is common in adults and can occur once in a while or as often as every day.</p>
<p><strong>What causes indigestion?</strong></p>
<p>Indigestion can be caused by a condition in the digestive tract such as gastroesophageal reflux disease (GERD), peptic ulcer disease, cancer, or abnormality of the pancreas or bile ducts. If the condition improves or resolves, the symptoms of indigestion usually improve.</p>
<p>Sometimes a person has indigestion for which a cause cannot be found. This type of indigestion, called functional dyspepsia, is thought to occur in the area where the stomach meets the small intestine. The indigestion may be related to abnormal motility — the squeezing or relaxing action — of the stomach muscle as it receives, digests, and moves food into the small intestine.</p>
<p><strong>What are the symptoms of indigestion?</strong></p>
<p>Most people with indigestion experience more than one of the following symptoms:</p>
<ul>
<li><strong>Fullness during a meal</strong> – The person feels overly full soon after the meal starts and cannot finish the meal.</li>
<li><strong>Bothersome fullness after a meal</strong> – The person feels overly full after a meal — it may feel like the food is staying in the stomach too long.</li>
<li><strong>Epigastric pain</strong> – The epigastric area is between the lower end of the chest bone and the navel. The person may experience epigastric pain ranging from mild to severe.</li>
<li><strong>Epigastric burning</strong> – The person feels an unpleasant sensation of heat in the epigastric area.</li>
</ul>
<p>Other, less frequent symptoms that may occur with indigestion are nausea and bloating — an unpleasant tightness in the stomach. Nausea and bloating could be due to causes other than indigestion.</p>
<p>Sometimes the term indigestion is used to describe the symptom of heartburn, but these are two different conditions. Heartburn is a painful, burning feeling in the chest that radiates toward the neck or back. Heartburn is caused by stomach acid rising into the esophagus and may be a symptom of GERD. A person can have symptoms of both indigestion and heartburn.</p>
<p><strong>How is indigestion diagnosed?</strong></p>
<p>To diagnose indigestion, the doctor asks about the person’s current symptoms and medical history and performs a physical examination. The doctor may order x rays of the stomach and small intestine.</p>
<p>The doctor may perform blood, breath, or stool tests if the type of bacteria that causes peptic ulcer disease is suspected as the cause of indigestion.</p>
<p>The doctor may perform an upper endoscopy. After giving a sedative to help the person become drowsy, the doctor passes an endoscope — a long, thin tube that has a light and small camera on the end — through the mouth and gently guides it down the esophagus into the stomach. The doctor can look at the esophagus and stomach with the endoscope to check for any abnormalities. The doctor may perform biopsies — removing small pieces of tissue for examination with a microscope — to look for possible damage from GERD or an infection.</p>
<p>Because indigestion can be a sign of a more serious condition, people should see a doctor right away if they experience:</p>
<ul>
<li>frequent vomiting</li>
<li>blood in vomit</li>
<li>weight loss or loss of appetite</li>
<li>black tarry stools</li>
<li>difficult or painful swallowing</li>
<li>abdominal pain in a non-epigastric area</li>
<li>indigestion accompanied by shortness of breath, sweating, or pain that radiates to the jaw, neck, or arm</li>
<li>symptoms that persist for more than 2 weeks</li>
</ul>
<p><strong>How is indigestion treated?</strong></p>
<p>Some people may experience relief from symptoms of indigestion by:</p>
<ul>
<li>eating several small, low-fat meals throughout the day at a slow pace</li>
<li>refraining from smoking</li>
<li>abstaining from consuming coffee, carbonated beverages, and alcohol</li>
<li>stopping use of medications that may irritate the stomach lining — such as aspirin or anti-inflammatory drugs</li>
<li>getting enough rest</li>
<li>finding ways to decrease emotional and physical stress, such as relaxation therapy or yoga</li>
</ul>
<p>The doctor may recommend over-the-counter antacids or medications that reduce acid production or help the stomach move food more quickly into the small intestine. Many of these medications can be purchased without a prescription. Nonprescription medications should only be used at the dose and for the length of time recommended on the label unless advised differently by a doctor. Informing the doctor when starting a new medication is important.</p>
<p><strong>Antacids</strong>, such as Alka-Seltzer, Maalox, Mylanta, Rolaids, and Riopan, are usually the first drugs recommended to relieve symptoms of indigestion. Many brands on the market use different combinations of three basic salts — magnesium, calcium, and aluminum — with hydroxide or bicarbonate ions to neutralize the acid in the stomach. Antacids, however, can have side effects. Magnesium salt can lead to diarrhea, and aluminum salt may cause constipation. Aluminum and magnesium salts are often combined in a single product to balance these effects. Calcium carbonate antacids, such as Tums, Titralac, and Alka-2, can also be a supplemental source of calcium, though they may cause constipation.</p>
<p><strong>H2 receptor antagonists (H2RAs)</strong> include ranitidine (Zantac), cimetidine (Tagamet), famotidine (Pepcid), and nizatidine (Axid) and are available both by prescription and over-the-counter. H2RAs treat symptoms of indigestion by reducing stomach acid. They work longer than but not as quickly as antacids. Side effects of H2RAs may include headache, nausea, vomiting, constipation, diarrhea, and unusual bleeding or bruising.</p>
<p><strong>Proton pump inhibitors (PPIs)</strong> include omeprazole (Prilosec, Zegerid), lansoprazole (Prevacid), pantoprazole (Protonix), rabeprazole (Aciphex), and esomeprazole (Nexium) and are available by prescription. Prilosec is also available in over-the-counter strength. PPIs, which are stronger than H2RAs, also treat indigestion symptoms by reducing stomach acid. PPIs are most effective in treating symptoms of indigestion in people who also have GERD. Side effects of PPIs may include back pain, aching, cough, headache, dizziness, abdominal pain, gas, nausea, vomiting, constipation, and diarrhea.</p>
<p><strong>Prokinetics</strong> such as metoclopramide (Reglan) may be helpful for people who have a problem with the stomach emptying too slowly. Metoclopramide also improves muscle action in the digestive tract. Prokinetics have frequent side effects that limit their usefulness, including fatigue, sleepiness, depression, anxiety, and involuntary muscle spasms or movements.</p>
<p>If testing shows the type of bacteria that causes peptic ulcer disease, the doctor may prescribe antibiotics to treat the condition.</p>
<p><strong>Points to Remember</strong></p>
<ul>
<li>Indigestion, also known as dyspepsia, is a term used to describe one or more symptoms including a feeling of fullness during a meal, uncomfortable fullness after a meal, and burning or pain in the upper abdomen.</li>
<li>Indigestion can be caused by a condition in the digestive tract such as gastroesophageal reflux disease (GERD), peptic ulcer disease, cancer, or abnormality of the pancreas or bile ducts.</li>
<li>Sometimes a person has indigestion for which a cause cannot be found. This type of indigestion is called functional dyspepsia.</li>
<li>Indigestion and heartburn are different conditions, but a person can have symptoms of both.</li>
<li>The doctor may order x rays; blood, breath, and stool tests; and an upper endoscopy with biopsies to diagnose indigestion.</li>
<li>Some people may experience relief from indigestion by making some lifestyle changes and decreasing stress.</li>
<li>The doctor may prescribe antacids, H2 receptor antagonists (H2RAs), proton pump inhibitors (PPIs), prokinetics, or antibiotics to treat the symptoms of indigestion.</li>
</ul>
<p><strong>Hope through Research</strong></p>
<p>The National Institute of Diabetes and Digestive and Kidney Diseases’ Division of Digestive Diseases and Nutrition supports basic and clinical research into gastrointestinal motility disorders, including indigestion. Further research is needed in the areas of</p>
<ul>
<li>the role of gastric hypersensitivity in indigestion</li>
<li>the relationship between indigestion and psychological disorders</li>
<li>new drug therapies for indigestion</li>
</ul>
