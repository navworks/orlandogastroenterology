				<h4>What is Eosinophilic Esophagitis</h4>
				<p>Eosinophilic Esophagitis is an immune-mediated inflammatory disorder that is often triggered in atopic individuals by food products or inhaled allergens. It is characterized by infiltration of the esophageal wall, including the epithelium, with mast cells and eosinophils. Clinically, the inflammatory response by mast cells and eosinophils within the esophagus results in symptoms of chest pain, heartburn, and dysphagia—the latter due either to peristaltic dysfunction or to mechanical narrowing of the lumen by a dense fibrotic reaction within the wall.</p>
				<p>Notably, infiltration of the esophagus by eosinophils has been shown to result from the release of eotaxin-3 and IL-4, IL-5, and IL-13 by squamous epithelial cells. This observation provides molecular targets for potential treatments for the condition. For instance, intravenous antibodies to IL-5 can reduce both blood and esophageal eosinophilia and improve the quality of life in patients with eosinophilic esophagitis.</p>
				<p>In 2006, a novel translational study identified the key role of eotaxin-3 in eosinophilic esophagitis. Utilizing a combination of microarray techniques, in vivo and in vitro studies, eotaxin-3 was shown to be significantly up-regulated compared to controls. Further studies showed mutation of this gene in affected patients, and basic analysis confirmed a significant role in this inflammatory process. In addition to potential molecular targets for treatment, these data add to the growing body of knowledge of the central role of the esophageal epithelium in generation of the inflammatory/ allergic cascade that ultimately translates into the clinical manifestations of the disease.</p>
				<h4>What are the symptoms of Eosinophilic Esophagitis?</h4>
				<p>Symptoms of eosinophilic esophagitis include vomiting, abdominal pain, poor growth, difficulty with swallowing, and food impaction. This condition is often misdiagnosed as GERD. Although the underlying etiology of eosinophilic esophagitis is often unidentified, foodborne and airborne allergens have been frequently implicated.</p>
				<h4>What is the treatment for Eosinophilic Esophagitis?</h4>
				<p>Currently, effective treatments for Eosinophilic Esophagitis are limited to steroids and diet restriction.</p>
