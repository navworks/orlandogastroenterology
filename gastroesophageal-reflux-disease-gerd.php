				 <h3 id="wha">What is GERD?</h3>




<p>Gastroesophageal reflux disease (GERD) is a more serious form of gastroesophageal reflux (GER), which is common. GER occurs when the lower esophageal sphincter (LES) opens spontaneously, for varying periods of time, or does not close properly and stomach contents rise up into the esophagus. GER is also called acid reflux or acid regurgitation, because digestive juices — called acids — rise up with the food. The esophagus is the tube that carries food from the mouth to the stomach. The LES is a ring of muscle at the bottom of the esophagus that acts like a valve between the esophagus and stomach.</p>
<p>When acid reflux occurs, food or fluid can be tasted in the back of the mouth. When refluxed stomach acid touches the lining of the esophagus it may cause a burning sensation in the chest or throat called heartburn or acid indigestion. Occasional GER is common and does not necessarily mean one has GERD. Persistent reflux that occurs more than twice a week is considered GERD, and it can eventually lead to more serious health problems. People of all ages can have GERD.</p>
<p><strong>What are the symptoms of GERD?</strong></p>
<p>The main symptom of GERD in adults is frequent heartburn, also called acid indigestion — burning-type pain in the lower part of the mid-chest, behind the breast bone, and in the mid-abdomen. Most children under 12 years with GERD, and some adults, have GERD without heartburn. Instead, they may experience a dry cough, asthma symptoms, or trouble swallowing.</p>
<p><strong>What causes GERD?</strong></p>
<p>The reason some people develop GERD is still unclear. However, research shows that in people with GERD, the LES relaxes while the rest of the esophagus is working. Anatomical abnormalities such as a hiatal hernia may also contribute to GERD. A hiatal hernia occurs when the upper part of the stomach and the LES move above the diaphragm, the muscle wall that separates the stomach from the chest. Normally, the diaphragm helps the LES keep acid from rising up into the esophagus. When a hiatal hernia is present, acid reflux can occur more easily. A hiatal hernia can occur in people of any age and is most often a normal finding in otherwise healthy people over age 50. Most of the time, a hiatal hernia produces no symptoms.</p>
<p>Other factors that may contribute to GERD include:</p>
<ul>
<li>obesity</li>
<li>pregnancy</li>
<li>smoking</li>
</ul>
<p>Common foods that can worsen reflux symptoms include:</p>
<ul>
<li>citrus fruits</li>
<li>chocolate</li>
<li>drinks with caffeine or alcohol</li>
<li>fatty and fried foods</li>
<li>garlic and onions</li>
<li>mint flavorings</li>
<li>spicy foods</li>
<li>tomato-based foods, like spaghetti sauce, salsa, chili, and pizza</li>
</ul>
<p><strong>What is GERD in children?</strong></p>
<p>Distinguishing between normal, physiologic reflux and GERD in children is important. Most infants with GER are happy and healthy even if they frequently spit up or vomit, and babies usually outgrow GER by their first birthday. Reflux that continues past 1 year of age may be GERD. Studies show GERD is common and may be overlooked in infants and children. For example, GERD can present as repeated regurgitation, nausea, heartburn, coughing, laryngitis, or respiratory problems like wheezing, asthma, or pneumonia. Infants and young children may demonstrate irritability or arching of the back, often during or immediately after feedings. Infants with GERD may refuse to feed and experience poor growth.</p>
<p>Talk with your child’s health care provider if reflux-related symptoms occur regularly and cause your child discomfort. Your health care provider may recommend simple strategies for avoiding reflux, such as burping the infant several times during feeding or keeping the infant in an upright position for 30 minutes after feeding. If your child is older, your health care provider may recommend that your child eat small, frequent meals and avoid the following foods:</p>
<ul>
<li>sodas that contain caffeine</li>
<li>chocolate</li>
<li>peppermint</li>
<li>spicy foods</li>
<li>acidic foods like oranges, tomatoes, and pizza</li>
<li>fried and fatty foods</li>
</ul>
<p>Avoiding food 2 to 3 hours before bed may also help. Your health care provider may recommend raising the head of your child’s bed with wood blocks secured under the bedposts. Just using extra pillows will not help. If these changes do not work, your health care provider may prescribe medicine for your child. In rare cases, a child may need surgery. For information about GER in infants, children, and adolescents, see the <a href="http://digestive.niddk.nih.gov/ddiseases/pubs/gerdinfant/index.htm" target="_blank">Gastroesophageal Reflux in Infants</a> and <a href="http://digestive.niddk.nih.gov/ddiseases/pubs/gerinchildren/index.htm" target="_blank">Gastroesophageal Reflux in Children and Adolescents</a> fact sheets from the National Institute of Diabetes and Digestive and Kidney Diseases (NIDDK).</p>
<p><strong>How is GERD treated?</strong></p>
<p>See your health care provider if you have had symptoms of GERD and have been using antacids or other over-the-counter reflux medications for more than 2 weeks. Your health care provider may refer you to a gastroenterologist, a doctor who treats diseases of the stomach and intestines. Depending on the severity of your GERD, treatment may involve one or more of the following lifestyle changes, medications, or surgery.</p>
<p><span style="text-decoration: underline;">Lifestyle Changes</span></p>
<ul>
<li>If you smoke, stop.</li>
<li>Avoid foods and beverages that worsen symptoms.</li>
<li>Lose weight if needed.</li>
<li>Eat small, frequent meals.</li>
<li>Wear loose-fitting clothes.</li>
<li>Avoid lying down for 3 hours after a meal.</li>
<li>Raise the head of your bed 6 to 8 inches by securing wood blocks under the bedposts. Just using extra pillows will not help.</li>
</ul>
<p><span style="text-decoration: underline;">Medications</span></p>
<p>Your health care provider may recommend over-the-counter antacids or medications that stop acid production or help the muscles that empty your stomach. You can buy many of these medications without a prescription. However, see your health care provider before starting or adding a medication.</p>
<p><strong>Antacids</strong>, such as Alka-Seltzer, Maalox, Mylanta, Rolaids, and Riopan, are usually the first drugs recommended to relieve heartburn and other mild GERD symptoms. Many brands on the market use different combinations of three basic salts — magnesium, calcium, and aluminum — with hydroxide or bicarbonate ions to neutralize the acid in your stomach. Antacids, however, can have side effects. Magnesium salt can lead to diarrhea, and aluminum salt may cause constipation. Aluminum and magnesium salts are often combined in a single product to balance these effects.</p>
<p>Calcium carbonate antacids, such as Tums, Titralac, and Alka-2, can also be a supplemental source of calcium. They can cause constipation as well.</p>
<p><strong>Foaming agents</strong>, such as Gaviscon, work by covering your stomach contents with foam to prevent reflux.</p>
<p><strong>H2 blockers</strong>, such as cimetidine (Tagamet HB), famotidine (Pepcid AC), nizatidine (Axid AR), and ranitidine (Zantac 75), decrease acid production. They are available in prescription strength and over-the-counter strength. These drugs provide short-term relief and are effective for about half of those who have GERD symptoms.</p>
<p><strong>Proton pump inhibitors</strong> include omeprazole (Prilosec, Zegerid), lansoprazole (Prevacid), pantoprazole (Protonix), rabeprazole (Aciphex), and esomeprazole (Nexium), which are available by prescription. Prilosec is also available in over-the-counter strength. Proton pump inhibitors are more effective than H2 blockers and can relieve symptoms and heal the esophageal lining in almost everyone who has GERD.</p>
<p><strong>Prokinetics</strong> help strengthen the LES and make the stomach empty faster. This group includes bethanechol (Urecholine) and metoclopramide (Reglan). Metoclopramide also improves muscle action in the digestive tract. Prokinetics have frequent side effects that limit their usefulness — fatigue, sleepiness, depression, anxiety, and problems with physical movement.</p>
<p>Because drugs work in different ways, combinations of medications may help control symptoms. People who get heartburn after eating may take both antacids and H2 blockers. The antacids work first to neutralize the acid in the stomach, and then the H2 blockers act on acid production. By the time the antacid stops working, the H2 blocker will have stopped acid production. Your health care provider is the best source of information about how to use medications for GERD.</p>
<p><strong>What if GERD symptoms persist?</strong></p>
<p>If your symptoms do not improve with lifestyle changes or medications, you may need additional tests.</p>
<ul>
<li><strong>Barium swallow radiograph</strong> uses x rays to help spot abnormalities such as a hiatal hernia and other structural or anatomical problems of the esophagus. With this test, you drink a solution and then x rays are taken. The test will not detect mild irritation, although strictures — narrowing of the esophagus — and ulcers can be observed.</li>
<li><strong>Upper endoscopy</strong> is more accurate than a barium swallow radiograph and may be performed in a hospital or a doctor’s office. The doctor may spray your throat to numb it and then, after lightly sedating you, will slide a thin, flexible plastic tube with a light and lens on the end called an endoscope down your throat. Acting as a tiny camera, the endoscope allows the doctor to see the surface of the esophagus and search for abnormalities. If you have had moderate to severe symptoms and this procedure reveals injury to the esophagus, usually no other tests are needed to confirm GERD. The doctor also may perform a biopsy. Tiny tweezers, called forceps, are passed through the endoscope and allow the doctor to remove small pieces of tissue from your esophagus. The tissue is then viewed with a microscope to look for damage caused by acid reflux and to rule out other problems if infection or abnormal growths are not found.</li>
<li><strong>pH monitoring examination</strong> involves the doctor either inserting a small tube into the esophagus or clipping a tiny device to the esophagus that will stay there for 24 to 48 hours. While you go about your normal activities, the device measures when and how much acid comes up into your esophagus. This test can be useful if combined with a carefully completed diary — recording when, what, and amounts the person eats — which allows the doctor to see correlations between symptoms and reflux episodes. The procedure is sometimes helpful in detecting whether respiratory symptoms, including wheezing and coughing, are triggered by reflux.</li>
</ul>
<p>A completely accurate diagnostic test for GERD does not exist, and tests have not consistently shown that acid exposure to the lower esophagus directly correlates with damage to the lining.</p>
<p><span style="text-decoration: underline;">Surgery</span></p>
<p>Surgery is an option when medicine and lifestyle changes do not help to manage GERD symptoms. Surgery may also be a reasonable alternative to a lifetime of drugs and discomfort.</p>
<p><strong>Fundoplication</strong> is the standard surgical treatment for GERD. Usually a specific type of this procedure, called Nissen fundoplication, is performed. During the Nissen fundoplication, the upper part of the stomach is wrapped around the LES to strengthen the sphincter, prevent acid reflux, and repair a hiatal hernia.</p>
<p>The Nissen fundoplication may be performed using a laparoscope, an instrument that is inserted through tiny incisions in the abdomen. The doctor then uses small instruments that hold a camera to look at the abdomen and pelvis. When performed by experienced surgeons, laparoscopic fundoplication is safe and effective in people of all ages, including infants. The procedure is reported to have the same results as the standard fundoplication, and people can leave the hospital in 1 to 3 days and return to work in 2 to 3 weeks.</p>
<p><strong>Endoscopic techniques</strong> used to treat chronic heartburn include the Bard EndoCinch system, NDO Plicator, and the Stretta system. These techniques require the use of an endoscope to perform the anti-reflux operation. The EndoCinch and NDO Plicator systems involve putting stitches in the LES to create pleats that help strengthen the muscle. The Stretta system uses electrodes to create tiny burns on the LES. When the burns heal, the scar tissue helps toughen the muscle. The longterm effects of these three procedures are unknown.</p>
<p><strong>What are the long-term complications of GERD?</strong></p>
<p>Chronic GERD that is untreated can cause serious complications. Inflammation of the esophagus from refluxed stomach acid can damage the lining and cause bleeding or ulcers — also called esophagitis. Scars from tissue damage can lead to strictures — narrowing of the esophagus — that make swallowing difficult. Some people develop Barrett’s esophagus, in which cells in the esophageal lining take on an abnormal shape and color. Over time, the cells can lead to esophageal cancer, which is often fatal. Persons with GERD and its complications should be monitored closely by a physician.</p>
<p>Studies have shown that GERD may worsen or contribute to asthma, chronic cough, and pulmonary fibrosis.</p>
<p>For information about Barrett’s esophagus, see the <a href="http://greaterorlandogi.com/services/barretts-esophagus/" target="_blank">Barrett’s esophagus fact sheet</a> from the NIDDK.</p>
<p><strong>Points to Remember</strong></p>
<ul>
<li>Frequent heartburn, also called acid indigestion, is the most common symptom of GERD in adults. Anyone experiencing heartburn twice a week or more may have GERD.</li>
<li>You can have GERD without having heartburn. Your symptoms could include a dry cough, asthma symptoms, or trouble swallowing.</li>
<li>If you have been using antacids for more than 2 weeks, it is time to see your health care provider. Most doctors can treat GERD. Your health care provider may refer you to a gastroenterologist, a doctor who treats diseases of the stomach and intestines.</li>
<li>Health care providers usually recommend lifestyle and dietary changes to relieve symptoms of GERD. Many people with GERD also need medication. Surgery may be considered as a treatment option.</li>
<li>Most infants with GER are healthy even though they may frequently spit up or vomit. Most infants outgrow GER by their first birthday. Reflux that continues past 1 year of age may be GERD.</li>
<li>The persistence of GER along with other symptoms — arching and irritability in infants, or abdominal and chest pain in older children — is GERD. GERD is the outcome of frequent and persistent GER in infants and children and may cause repeated vomiting, coughing, and respiratory problems.</li>
</ul>
