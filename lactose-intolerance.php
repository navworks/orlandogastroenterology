				 <h3 id="wha">What is lactose?</h3>
<p>Lactose is a sugar found in milk and milk products. The small intestine—the organ where most food digestion and nutrient absorption take place—produces an enzyme called lactase. Lactase breaks down lactose into two simpler forms of sugar: glucose and galactose. The body then absorbs these simpler sugars into the bloodstream.</p>
	
<p>[<a class="top" href="http://digestive.niddk.nih.gov/ddiseases/pubs/lactoseintolerance/index.aspx#top">Top</a>]</p>
<h3 id="what">What is lactose intolerance?</h3>			  

<p>Lactose intolerance is a condition in which people have digestive symptoms—such as bloating, diarrhea, and gas—after eating or drinking milk or milk products.</p>
<p>Lactase deficiency and lactose malabsorption may lead to lactose intolerance:</p>
<ul>
<li><strong>Lactase deficiency.</strong> In people who have a lactase deficiency, the small intestine produces low levels of lactase and cannot digest much lactose.</li>
<li><strong>Lactose malabsorption.</strong> Lactase deficiency may cause lactose malabsorption. In lactose malabsorption, undigested lactose passes to the colon. The colon, part of the large intestine, absorbs water from stool and changes it from a liquid to a solid form. In the colon, bacteria break down undigested lactose and create fluid and gas. Not all people with lactase deficiency and lactose malabsorption have digestive symptoms.</li>
</ul>
<p></p>
<p><span class="caption">The digestive tract</span></p>
<p>People have lactose intolerance when lactase deficiency and lactose malabsorption cause digestive symptoms. Most people with lactose intolerance can eat or drink some amount of lactose without having digestive symptoms. Individuals vary in the amount of lactose they can tolerate.</p>
<p>People sometimes confuse lactose intolerance with a milk allergy. While lactose intolerance is a digestive system disorder, a milk allergy is a reaction by the body’s immune system to one or more milk proteins. An allergic reaction to milk can be life threatening even if the person eats or drinks only a small amount of milk or milk product. A milk allergy most commonly occurs in the first year of life, while lactose intolerance occurs more often during adolescence or adulthood.<a href="http://digestive.niddk.nih.gov/ddiseases/pubs/lactoseintolerance/index.aspx#sup1"><sup>1</sup></a><sup>,</sup><a href="http://digestive.niddk.nih.gov/ddiseases/pubs/lactoseintolerance/index.aspx#sup2"><sup>2</sup></a></p>
<p id="sup1"><sup>1</sup><span class="caption">Boyce JA, Assa’ad A, Burks AW, et al. Guidelines for the diagnosis and management of food allergy in the United States: report of the NIAID-sponsored expert panel. <em>Journal of Allergy and Clinical Immunology.</em> 2010;126(6 suppl.):S1–S58.</span></p>
<p id="sup2"><sup>2</sup><span class="caption">Suchy FJ, Brannon PM, Carpenter TO, et al. National Institutes of Health Consensus Development Conference: lactose intolerance and health. <em>Annals of Internal Medicine.</em> 2010;152(12):792–796.</span></p>

<p></p>
