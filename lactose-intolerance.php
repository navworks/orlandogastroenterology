<h3 id="are">What are the symptoms of lactose intolerance?</h3>
<p>Common symptoms of lactose intolerance include</p>
<ul>
<li>abdominal bloating, a feeling of fullness or swelling in the abdomen</li>
<li>abdominal pain</li>
<li>diarrhea</li>
<li>gas</li>
<li>nausea</li>
</ul>
<p>Symptoms occur 30 minutes to 2 hours after consuming milk or milk products. Symptoms range from mild to severe based on the amount of lactose the person ate or drank and the amount a person can tolerate.</p>
<p>[<a class="top" href="http://digestive.niddk.nih.gov/ddiseases/pubs/lactoseintolerance/index.aspx#top">Top</a>]</p>
<h3 id="how">How does lactose intolerance affect health?</h3>
<p>In addition to causing unpleasant symptoms, lactose intolerance may affect people’s health if it keeps them from consuming enough essential nutrients, such as calcium and vitamin D. People with lactose intolerance may not get enough calcium if they do not eat calcium-rich foods or do not take a dietary supplement that contains calcium. Milk and milk products are major sources of calcium and other nutrients in the diet. Calcium is essential at all ages for the growth and maintenance of bones. A shortage of calcium intake in children and adults may lead to bones that are less dense and can easily fracture later in life, a condition called osteoporosis.</p>
<p>[<a class="top" href="http://digestive.niddk.nih.gov/ddiseases/pubs/lactoseintolerance/index.aspx#top">Top</a>]</p>
<h3 id="howis">How is lactose intolerance diagnosed?</h3>
<p>A health care provider makes a diagnosis of lactose intolerance based on</p>
<ul>
<li>medical, family, and diet history, including a review of symptoms</li>
<li>a physical exam</li>
<li>medical tests</li>
</ul>
<p><strong>Medical, family, and diet history.</strong> A health care provider will take a medical, family, and diet history to help diagnose lactose intolerance. During this discussion, the health care provider will review a patient’s symptoms. However, basing a diagnosis on symptoms alone may be misleading because digestive symptoms can occur for many reasons other than lactose intolerance. For example, other conditions such as irritable bowel syndrome, celiac disease, inflammatory bowel disease, or small bowel bacterial overgrowth can cause digestive symptoms.</p>
<p><strong>Physical exam.</strong> A physical exam may help diagnose lactose intolerance or rule out other conditions that cause digestive symptoms. During a physical exam, a health care provider usually</p>
<ul>
<li>checks for abdominal bloating</li>
<li>uses a stethoscope to listen to sounds within the abdomen</li>
<li>taps on the abdomen to check for tenderness or pain</li>
</ul>
<p>A health care provider may recommend eliminating all milk and milk products from a person’s diet for a short time to see if the symptoms resolve. Symptoms that go away when a person eliminates lactose from his or her diet may confirm the diagnosis of lactose intolerance.</p>
<p><strong>Medical tests.</strong> A health care provider may order special tests to provide more information. Health care providers commonly use two tests to measure how well a person digests lactose:</p>
<ul>
<li><strong>Hydrogen breath test.</strong> This test measures the amount of hydrogen in a person’s breath. Normally, only a small amount of hydrogen is detectable in the breath when a person eats or drinks and digests lactose. However, undigested lactose produces high levels of hydrogen. For this test, the patient drinks a beverage that contains a known amount of lactose. A health care provider asks the patient to breathe into a balloon-type container that measures breath hydrogen level. In most cases, a health care provider performs this test at a hospital, on an outpatient basis. Smoking and some foods and medications may affect the accuracy of the results. A health care provider will tell the patient what foods or medications to avoid before the test.</li>
<li><strong>Stool acidity test.</strong> Undigested lactose creates lactic acid and other fatty acids that a stool acidity test can detect in a stool sample. Health care providers sometimes use this test to check acidity in the stools of infants and young children. A child may also have glucose in his or her stool as a result of undigested lactose. The health care provider will give the child’s parent or caretaker a container for collecting the stool specimen. The parent or caretaker returns the sample to the health care provider, who sends it to a lab for analysis.</li>
</ul>
<p>[<a class="top" href="http://digestive.niddk.nih.gov/ddiseases/pubs/lactoseintolerance/index.aspx#top">Top</a>]</p>
<h3 id="cana">How much lactose can a person with lactose intolerance have?</h3>
<p>Most people with lactose intolerance can tolerate some amount of lactose in their diet and do not need to avoid milk or milk products completely. Avoiding milk and milk products altogether may cause people to take in less calcium and vitamin D than they need. See the “Calcium and Vitamin D” section.</p>
<p>Individuals vary in the amount of lactose they can tolerate. A variety of factors—including how much lactase the small intestine produces—can affect how much lactose an individual can tolerate. For example, one person may have severe symptoms after drinking a small amount of milk, while another person can drink a large amount without having symptoms. Other people can easily eat yogurt and hard cheeses such as cheddar and Swiss, while they are not able to eat or drink other milk products without having digestive symptoms.</p>
<p>Research suggests that adults and adolescents with lactose malabsorption could eat or drink at least 12 grams of lactose in one sitting without symptoms or with only minor symptoms. This amount is the amount of lactose in 1 cup of milk. People with lactose malabsorption may be able to eat or drink more lactose if they eat it or drink it with meals or in small amounts throughout the day.<a href="http://digestive.niddk.nih.gov/ddiseases/pubs/lactoseintolerance/index.aspx#sup2"><sup>2</sup></a></p>
<p>[<a class="top" href="http://digestive.niddk.nih.gov/ddiseases/pubs/lactoseintolerance/index.aspx#top">Top</a>]</p>
<h3 id="mana">How is lactose intolerance managed?</h3>
<p>Many people can manage the symptoms of lactose intolerance by changing their diet. Some people may only need to limit the amount of lactose they eat or drink. Others may need to avoid lactose altogether. Using lactase products can help some people manage their symptoms.</p>
<p>For people with secondary lactase deficiency, treating the underlying cause improves lactose tolerance. In infants with developmental lactase deficiency, the ability to digest lactose improves as the infants mature. People with primary and congenital lactase deficiency cannot change their body’s ability to produce lactase.</p>
<p>[<a class="top" href="http://digestive.niddk.nih.gov/ddiseases/pubs/lactoseintolerance/index.aspx#top">Top</a>]</p>
<h4 id="eating">Eating, Diet, and Nutrition</h4>
<p>People may find it helpful to talk with a health care provider or a registered dietitian about a dietary plan. A dietary plan can help people manage the symptoms of lactose intolerance and make sure they get enough nutrients. Parents, caretakers, childcare providers, and others who serve food to children with lactose intolerance should follow the dietary plan recommended by the child’s health care provider or registered dietitian.</p>
<p><strong>Milk and milk products.</strong> Gradually introducing small amounts of milk or milk products may help some people adapt to them with fewer symptoms. Often, people can better tolerate milk or milk products by having them with meals, such as having milk with cereal or having cheese with crackers. People with lactose intolerance are generally more likely to tolerate hard cheeses, such as cheddar or Swiss, than a glass of milk. A 1.5‑ounce serving of low-fat hard cheese has less than 1 gram of lactose, while a 1-cup serving of low-fat milk has about 11 to 13 grams of lactose.<a href="http://digestive.niddk.nih.gov/ddiseases/pubs/lactoseintolerance/index.aspx#sup2"><sup>2</sup></a></p>
<p>However, people with lactose intolerance are also more likely to tolerate yogurt than milk, even though yogurt and milk have similar amounts of lactose.<a href="http://digestive.niddk.nih.gov/ddiseases/pubs/lactoseintolerance/index.aspx#sup2"><sup>2</sup></a></p>
<p><strong>Lactose-free and lactose-reduced milk and milk products.</strong> Lactose-free and lactose-reduced milk and milk products are available at most supermarkets and are identical nutritionally to regular milk and milk products. Manufacturers treat lactose-free milk with the lactase enzyme. This enzyme breaks down the lactose in the milk. Lactose-free milk remains fresh for about the same length of time or, if it is ultra-pasteurized, longer than regular milk. Lactose-free milk may have a slightly sweeter taste than regular milk.</p>
<p><strong>Lactase products.</strong> People can use lactase tablets and drops when they eat or drink milk products. The lactase enzyme digests the lactose in the food and therefore reduces the chances of developing digestive symptoms. People should check with a health care provider before using these products because some groups, such as young children and pregnant and breastfeeding women, may not be able to use them.</p>
<p>[<a class="top" href="http://digestive.niddk.nih.gov/ddiseases/pubs/lactoseintolerance/index.aspx#top">Top</a>]</p>
