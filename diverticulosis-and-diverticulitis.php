<p><strong>When is surgery necessary for diverticulitis?</strong></p>
<p>If symptoms of diverticulitis are frequent, or the patient does not respond to antibiotics and resting the colon, the doctor may advise surgery. The surgeon removes the affected part of the colon and joins the remaining sections. This type of surgery — called colon resection — aims to prevent complications and future diverticulitis. The doctor may also recommend surgery for complications such as a fistula or partial intestinal obstruction.</p>
<p>Immediate surgery may be necessary when the patient has other complications, such as perforation, a large abscess, peritonitis, complete intestinal obstruction, or severe bleeding. In these cases, two surgeries may be needed because it is not safe to rejoin the colon right away. During the first surgery, the surgeon cleans the infected abdominal cavity, removes the portion of the affected colon, and performs a temporary colostomy, creating an opening, or stoma, in the abdomen. The end of the colon is connected to the opening to allow normal eating while healing occurs. Stool is collected in a pouch attached to the stoma. In the second surgery several months later, the surgeon rejoins the ends of the colon and closes the stoma.</p>
<p>&nbsp;</p>
<p><strong>Points to Remember</strong></p>
<ul>
<li>Diverticulosis occurs when small pouches called diverticula bulge outward through weak spots in the colon, or large intestine.</li>
<li>Most people with diverticulosis never have any discomfort or symptoms.</li>
<li>Diverticula form when pressure builds inside the colon wall, usually because of constipation.</li>
<li>The most likely cause of diverticulosis is a low-fiber diet because it increases constipation and pressure inside the colon.</li>
<li>For most people with diverticulosis, eating a high-fiber diet is the only treatment needed.</li>
<li>Fiber intake can be increased by eating whole-grain breads and cereals; fruits like apples and pears; vegetables like peas, spinach, and squash; and starchy vegetables like kidney and black beans.</li>
<li>Diverticulitis occurs when the pouches become inflamed and cause pain and tenderness in the lower left side of the abdomen.</li>
<li>Diverticulitis can lead to bleeding; infections; small tears, called perforations; or blockages in the colon. These complications always require treatment to prevent them from progressing and causing serious illness.</li>
<li>Severe cases of diverticulitis with acute pain and complications will likely require a hospital stay. When a person has complications or does not respond to medication, surgery may be necessary.</li>
</ul>
<p><strong>Hope through Research</strong></p>
<p>The National Institute of Diabetes and Digestive and Kidney Diseases and the National Cancer Institute sponsor research programs to investigate diverticulosis and diverticulitis. Investigation continues in several areas, including:</p>
<ul>
<li>a possible link between diverticular disease and inflammatory bowel disease</li>
<li>the management of recurrent diverticular disease</li>
<li>the use of probiotics in the prevention and treatment of diverticular disease</li>
<li>indications for surgery for uncomplicated diverticulitis</li>
</ul>
