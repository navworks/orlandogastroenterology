				 <h3 id="wha">What are diverticulosis and diverticulitis?</h3>



<p>Many people have small pouches in the lining of the colon, or large intestine, that bulge outward through weak spots. Each pouch is called a diverticulum. Multiple pouches are called diverticula. The condition of having diverticula is called diverticulosis. About 10 percent of Americans older than 40 have diverticulosis. The condition becomes more common as people age. About half of all people older than 60 have diverticulosis.</p>
<p>Many people have small pouches in their colon that bulge outward through weak spots. Each pouch is called a diverticulum. Multiple pouches are called diverticula. The condition of having diverticula is called diverticulosis.</p>
<p>Diverticula are most common in the lower portion of the large intestine, called the sigmoid colon. When the pouches become inflamed, the condition is called diverticulitis. Ten to 25 percent of people with diverticulosis get diverticulitis. Diverticulosis and diverticulitis together are called diverticular disease.</p>
<p>&nbsp;</p>
<p><strong>What are the symptoms of diverticulosis and diverticulitis?</strong></p>
<p><span style="text-decoration: underline;">Diverticulosis</span></p>
<p>Most people with diverticulosis do not have any discomfort or symptoms. However, some people may experience crampy pain or discomfort in the lower abdomen, bloating, and constipation. Other conditions such as irritable bowel syndrome and stomach ulcers cause similar problems, so the symptoms do not always mean a person has diverticulosis. People with chronic symptoms should visit their doctor or health care provider.</p>
<p><span style="text-decoration: underline;">Diverticulitis</span></p>
<p>The most common symptom of diverticulitis is abdominal pain. The most common sign on examination is tenderness in the lower left side of the abdomen. Usually, the pain is severe and comes on suddenly, but it can also be mild and become worse over several days. The intensity of the pain can fluctuate. A person may experience cramping, nausea, vomiting, fever, chills, or a change in bowel habits.</p>
<p>&nbsp;</p>
<p><strong>What are the complications of diverticulitis?</strong></p>
<p>Diverticulitis can lead to bleeding; infections; small tears, called perforations; or blockages in the colon. These complications always require treatment to prevent them from progressing and causing serious illness.</p>
<p><span style="text-decoration: underline;">Bleeding</span></p>
<p>Rectal bleeding from diverticula is a rare complication. Doctors believe the bleeding is caused by a small blood vessel in a diverticulum that weakens and then bursts. When diverticula bleed, blood may appear in the toilet or in the stool. Bleeding can be severe, but it may stop by itself and not require treatment. A person who has bleeding from the rectum — even a small amount — should see a doctor right away. Often, colonoscopy is used to identify the site of bleeding and stop the bleeding. Sometimes the doctor injects dye into an artery — a procedure called angiography — to identify and treat diverticular bleeding. If the bleeding does not stop, surgery may be necessary to remove the involved portion of the colon.</p>
<p><span style="text-decoration: underline;">Abscess, Perforation, and Peritonitis</span></p>
<p>Diverticulitis may lead to infection, which often clears up after a few days of treatment with antibiotics. If the infection gets worse, an abscess may form in the wall of the colon.</p>
<p>An abscess is a localized collection of pus that may cause swelling and destroy tissue. If the abscess is small and remains in the wall of the colon, it may clear up after treatment with antibiotics. If the abscess does not clear up with antibiotics, the doctor may need to drain it using a catheter — a small tube — placed into the abscess through the skin. After giving the patient numbing medicine, the doctor inserts the needle through the skin until reaching the abscess and then drains the fluid through the catheter. This process may be guided by sonography or x-ray.</p>
<p>Infected diverticula may develop perforations. Sometimes the perforations leak pus out of the colon and form a large abscess in the abdominal cavity, a condition called peritonitis. A person with peritonitis may be extremely ill with nausea, vomiting, fever, and severe abdominal tenderness. The condition requires immediate surgery to clean the abdominal cavity and remove the damaged part of the colon. Without prompt treatment, peritonitis can be fatal.</p>
<p><span style="text-decoration: underline;">Fistula</span></p>
<p>A fistula is an abnormal connection of tissue between two organs or between an organ and the skin. When damaged tissues come into contact with each other during infection, they sometimes stick together. If they heal that way, a fistula may form. When diverticulitis-related infection spreads outside the colon, the colon’s tissue may stick to nearby tissues. The organs usually involved are the bladder, small intestine, and skin.</p>
<p>The most common type of fistula occurs between the bladder and the colon. This type of fistula affects men more often than women. It can result in a severe, long-lasting infection of the urinary tract. The problem can be corrected with surgery to remove the fistula and the affected part of the colon.</p>
<p><span style="text-decoration: underline;">Intestinal Obstruction</span></p>
<p>Scarring caused by infection may lead to partial or total blockage of the intestine, called intestinal obstruction. When the intestine is blocked, the colon is unable to move bowel contents normally. If the intestine is completely blocked, emergency surgery is necessary. Partial blockage is not an emergency, so the surgery to correct it can be planned.</p>
<p>&nbsp;</p>
<p><strong>What causes diverticular disease?</strong></p>
<p>Although not proven, the dominant theory is that a low-fiber diet causes diverticular disease. The disease was first noticed in the United States in the early 1900s, around the time processed foods were introduced into the American diet. Consumption of processed foods greatly reduced Americans’ fiber intake.</p>
<p>Diverticular disease is common in developed or industrialized countries — particularly the United States, England, and Australia — where low-fiber diets are consumed. The disease is rare in Asia and Africa, where most people eat high-fiber diets.</p>
<p>Fiber is the part of fruits, vegetables, and grains that the body cannot digest. Some fiber, called soluble fiber, dissolves easily in water. It takes on a soft, jelly-like texture in the intestines. Insoluble fiber passes almost unchanged through the intestines. Both kinds of fiber help prevent constipation by making stools soft and easy to pass.</p>
<p>Constipation — or hard stool — may cause people to strain when passing stool during a bowel movement. Straining may cause increased pressure in the colon, which may cause the colon lining to bulge out through weak spots in the colon wall. These bulges are diverticula.</p>
<p>Lack of exercise also may be associated with a greater risk of forming diverticula, although the reasons for this are not well understood.</p>
<p>Doctors are not certain what causes diverticula to become inflamed. The inflammation may begin when bacteria or stool are caught in the diverticula. An attack of diverticulitis can develop suddenly and without warning.</p>
<p>&nbsp;</p>
<p><strong>How is diverticular disease diagnosed?</strong></p>
<p>To diagnose diverticular disease, the doctor asks about medical history, does a physical exam, and may perform one or more diagnostic tests. Because most people do not have symptoms, diverticulosis is often found through tests ordered for another ailment. For example, diverticulosis is often found during a colonoscopy done to screen for cancer or polyps or to evaluate complaints of pain or rectal bleeding.</p>
<p>When taking a medical history, the doctor may ask about bowel habits, pain, other symptoms, diet, and medications. The physical exam usually involves a digital rectal exam. To perform this test, the doctor inserts a gloved, lubricated finger into the rectum to detect tenderness, blockage, or blood. The doctor may check stool for signs of bleeding and test blood for signs of infection. If diverticulitis is suspected, the doctor may order one of the following radiologic tests:</p>
<ul>
<li><strong>Abdominal ultrasound</strong> – Sound waves are sent toward the colon through a handheld device that a technician glides over the abdomen. The sound waves bounce off the colon and other organs, and their echoes make electrical impulses that create a picture — called a sonogram — on a video monitor. If the diverticula are inflamed, the sound waves will also bounce off of them, showing their location.</li>
<li><strong>Computerized tomography (CT) scan</strong> – The CT scan is a noninvasive x-ray that produces cross-section images of the body. The doctor may inject dye into a vein and the person may be given a similar mixture to swallow. The person lies on a table that slides into a donut-shaped machine. The dye helps to show complications of diverticulitis such as perforations and abscesses.</li>
</ul>
<p>&nbsp;</p>
<p><strong>How is diverticular disease treated?</strong></p>
<p>A high-fiber diet and pain medications help relieve symptoms in most cases of diverticulosis. Uncomplicated diverticulitis with mild symptoms usually requires the person to rest, take oral antibiotics, and be on a liquid diet for a period of time. Sometimes an attack of diverticulitis is serious enough to require a hospital stay, intravenous (IV) antibiotics, and possibly surgery.</p>
<p><span style="text-decoration: underline;">Diverticulosis</span></p>
<p>Increasing the amount of fiber in the diet may reduce symptoms of diverticulosis and prevent complications such as diverticulitis. Fiber keeps stool soft and lowers pressure inside the colon so that bowel contents can move through easily. The American Dietetic Association recommends consuming 20 to 35 grams of fiber each day. The table “What foods have fiber?” shows the amount of fiber in some foods that a person can easily add to the diet. The doctor may also recommend taking a fiber product such as methylcellulose (Citrucel) or psyllium (Metamucil) one to three times a day. These products are available in powder, pills, or wafers, and provide 2 to 3.4 grams of fiber per dose. Fiber products should be taken with at least 8 ounces of water.</p>
<p>Avoidance of nuts, popcorn, and sunflower, pumpkin, caraway, and sesame seeds has been recommended by physicians out of fear that food particles could enter, block, or irritate the diverticula. However, no scientific data support this treatment measure. Eating a high-fiber diet is the only requirement highly emphasized across the medical literature. Eliminating specific foods is not necessary. The seeds in tomatoes, zucchini, cucumbers, strawberries, and raspberries, as well as poppy seeds, are generally considered harmless. People differ in the amounts and types of foods they can eat. Decisions about diet should be made based on what works best for each person. Keeping a food diary may help identify what foods may cause symptoms.</p>
<p>If cramps, bloating, and constipation are problems, the doctor may prescribe a short course of pain medication. However, some pain medications actually cause constipation.</p>
<p><span style="text-decoration: underline;">Diverticulitis</span></p>
<p>Treatment for diverticulitis focuses on clearing up the inflammation and infection, resting the colon, and preventing or minimizing complications.</p>
<p>Depending on the severity of symptoms, the doctor may recommend bed rest, oral antibiotics, a pain reliever, and a liquid diet. If symptoms ease after a few days, the doctor will recommend gradually increasing the amount of high-fiber foods in the diet.</p>
<p>Severe cases of diverticulitis with acute pain and complications will likely require a hospital stay. Most cases of severe diverticulitis are treated with IV antibiotics and a few days without food or drink to help the colon rest. In some cases, surgery may be necessary.</p>
