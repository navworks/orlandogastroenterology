<p>Gallstones are small, pebble-like substances that develop in the gallbladder. The gallbladder is a small, pear-shaped sac located below your liver in the right upper abdomen. Gallstones form when liquid stored in the gallbladder hardens into pieces of stone-like material. The liquid — called bile — helps the body digest fats. Bile is made in the liver, then stored in the gallbladder until the body needs it. The gallbladder contracts and pushes the bile into a tube — called the common bile duct — that carries it to the small intestine, where it helps with digestion.</p>
<p>Bile contains water, cholesterol, fats, bile salts, proteins, and bilirubin — a waste product. Bile salts break up fat, and bilirubin gives bile and stool a yellowish-brown color. If the liquid bile contains too much cholesterol, bile salts, or bilirubin, it can harden into gallstones.</p>
<p>The two types of gallstones are cholesterol stones and pigment stones. Cholesterol stones are usually yellow-green and are made primarily of hardened cholesterol. They account for about 80 percent of gallstones. Pigment stones are small, dark stones made of bilirubin. Gallstones can be as small as a grain of sand or as large as a golf ball. The gallbladder can develop just one large stone, hundreds of tiny stones, or a combination of the two.</p>
<p>The gallbladder and the ducts that carry bile and other digestive enzymes from the liver, gallbladder, and pancreas to the small intestine are called the biliary system.</p>
<p>Gallstones can block the normal flow of bile if they move from the gallbladder and lodge in any of the ducts that carry bile from the liver to the small intestine. The ducts include the:</p>
<ul  class="arrow-list">
<li>hepatic ducts, which carry bile out of the liver</li>
<li>cystic duct, which takes bile to and from the gallbladder</li>
<li>common bile duct, which takes bile from the cystic and hepatic ducts to the small intestine</li>
</ul>
<p>Bile trapped in these ducts can cause inflammation in the gallbladder, the ducts, or in rare cases, the liver. Other ducts open into the common bile duct, including the pancreatic duct, which carries digestive enzymes out of the pancreas. Sometimes gallstones passing through the common bile duct provoke inflammation in the pancreas — called gallstone pancreatitis — an extremely painful and potentially dangerous condition.</p>
<p>If any of the bile ducts remain blocked for a significant period of time, severe damage or infection can occur in the gallbladder, liver, or pancreas. Left untreated, the condition can be fatal. Warning signs of a serious problem are fever, jaundice, and persistent pain.</p>
<p><strong>What causes gallstones?</strong></p>
<p>Scientists believe cholesterol stones form when bile contains too much cholesterol, too much bilirubin, or not enough bile salts, or when the gallbladder does not empty completely or often enough. The reason these imbalances occur is not known.</p>
<p>The cause of pigment stones is not fully understood. The stones tend to develop in people who have liver cirrhosis, biliary tract infections, or hereditary blood disorders — such as sickle cell anemia — in which the liver makes too much bilirubin.</p>
<p>The mere presence of gallstones may cause more gallstones to develop. Other factors that contribute to the formation of gallstones, particularly cholesterol stones, include:</p>
<ul  class="arrow-list">
<li><strong>Sex</strong> – Women are twice as likely as men to develop gallstones. Excess estrogen from pregnancy, hormone replacement therapy, and birth control pills appears to increase cholesterol levels in bile and decrease gallbladder movement, which can lead to gallstones.</li>
<li><strong>Family history</strong> – Gallstones often run in families, pointing to a possible genetic link.</li>
<li><strong>Weight</strong> – A large clinical study showed that being even moderately overweight increases the risk for developing gallstones. The most likely reason is that the amount of bile salts in bile is reduced, resulting in more cholesterol. Increased cholesterol reduces gallbladder emptying. Obesity is a major risk factor for gallstones, especially in women.</li>
<li><strong>Diet</strong> – Diets high in fat and cholesterol and low in fiber increase the risk of gallstones due to increased cholesterol in the bile and reduced gallbladder emptying.</li>
<li><strong>Rapid weight loss</strong> – As the body metabolizes fat during prolonged fasting and rapid weight loss — such as “crash diets” — the liver secretes extra cholesterol into bile, which can cause gallstones. In addition, the gallbladder does not empty properly.</li>
<li><strong>Age</strong> – People older than age 60 are more likely to develop gallstones than younger people. As people age, the body tends to secrete more cholesterol into bile.</li>
<li><strong>Ethnicity</strong> – American Indians have a genetic predisposition to secrete high levels of cholesterol in bile. In fact, they have the highest rate of gallstones in the United States. The majority of American Indian men have gallstones by age 60. Among the Pima Indians of Arizona, 70 percent of women have gallstones by age 30. Mexican American men and women of all ages also have high rates of gallstones.</li>
<li><strong>Cholesterol-lowering drugs</strong> – Drugs that lower cholesterol levels in the blood actually increase the amount of cholesterol secreted into bile. In turn, the risk of gallstones increases.</li>
<li><strong>Diabetes</strong> – People with diabetes generally have high levels of fatty acids called triglycerides. These fatty acids may increase the risk of gallstones.</li>
</ul>
<p><strong>Who is at risk for gallstones?</strong></p>
<p>People at risk for gallstones include:</p>
<ul  class="arrow-list">
<li>women — especially women who are pregnant, use hormone replacement therapy, or take birth control pills</li>
<li>people over age 60</li>
<li>American Indians</li>
<li>Mexican Americans</li>
<li>overweight or obese men and women</li>
<li>people who fast or lose a lot of weight quickly</li>
<li>people with a family history of gallstones</li>
<li>people with diabetes</li>
<li>people who take cholesterol-lowering drugs</li>
</ul>
<p><strong>What are the symptoms of gallstones?</strong></p>
<p>As gallstones move into the bile ducts and create blockage, pressure increases in the gallbladder and one or more symptoms may occur. Symptoms of blocked bile ducts are often called a gallbladder “attack” because they occur suddenly. Gallbladder attacks often follow fatty meals, and they may occur during the night. A typical attack can cause:</p>
<ul  class="arrow-list">
<li>steady pain in the right upper abdomen that increases rapidly and lasts from 30 minutes to several hours</li>
<li>pain in the back between the shoulder blades</li>
<li>pain under the right shoulder</li>
</ul>
<p>Notify your doctor if you think you have experienced a gallbladder attack. Although these attacks often pass as gallstones move, your gallbladder can become infected and rupture if a blockage remains.</p>
<p>People with any of the following symptoms should see a doctor immediately:</p>
<ul  class="arrow-list">
<li>prolonged pain — more than 5 hours</li>
<li>nausea and vomiting</li>
<li>fever — even low-grade — or chills</li>
<li>yellowish color of the skin or whites of the eyes</li>
<li>clay-colored stools</li>
</ul>
<p>Many people with gallstones have no symptoms; these gallstones are called “silent stones.” They do not interfere with gallbladder, liver, or pancreas function and do not need treatment.</p>
<p><strong>How are gallstones diagnosed?</strong></p>
<p>Frequently, gallstones are discovered during tests for other health conditions. When gallstones are suspected to be the cause of symptoms, the doctor is likely to do an ultrasound exam — the most sensitive and specific test for gallstones. A handheld device, which a technician glides over the abdomen, sends sound waves toward the gallbladder. The sound waves bounce off the gallbladder, liver, and other organs, and their echoes make electrical impulses that create a picture of the gallbladder on a video monitor. If gallstones are present, the sound waves will bounce off them, too, showing their location. Other tests may also be performed.</p>
<ul  class="arrow-list">
<li><strong>Computerized tomography (CT) scan</strong> – The CT scan is a noninvasive x ray that produces cross-section images of the body. The test may show the gallstones or complications, such as infection and rupture of the gallbladder or bile ducts.</li>
<li><strong>Cholescintigraphy (HIDA scan)</strong> – The patient is injected with a small amount of non-harmful radioactive material that is absorbed by the gallbladder, which is then stimulated to contract. The test is used to diagnose abnormal contraction of the gallbladder or obstruction of the bile ducts.</li>
<li><strong>Endoscopic retrograde cholangiopancreatography (ERCP)</strong> – ERCP is used to locate and remove stones in the bile ducts. After lightly sedating you, the doctor inserts an endoscope — a long, flexible, lighted tube with a camera — down the throat and through the stomach and into the small intestine. The endoscope is connected to a computer and video monitor. The doctor guides the endoscope and injects a special dye that helps the bile ducts appear better on the monitor. The endoscope helps the doctor locate the affected bile duct and the gallstone. The stone is captured in a tiny basket and removed with the endoscope.</li>
<li><strong>Blood tests</strong> – Blood tests may be performed to look for signs of infection, obstruction, pancreatitis, or jaundice.</li>
</ul>
<p><strong>How are gallstones treated?</strong></p>
<p><span style="text-decoration: underline;">Surgery</span></p>
<p>If you have gallstones without symptoms, you do not require treatment. If you are having frequent gallbladder attacks, your doctor will likely recommend you have your gallbladder removed — an operation called a cholecystectomy. Surgery to remove the gallbladder — a nonessential organ — is one of the most common surgeries performed on adults in the United States.</p>
<p>Nearly all cholecystectomies are performed with laparoscopy. After giving you medication to sedate you, the surgeon makes several tiny incisions in the abdomen and inserts a laparoscope and a miniature video camera. The camera sends a magnified image from inside the body to a video monitor, giving the surgeon a close-up view of the organs and tissues. While watching the monitor, the surgeon uses the instruments to carefully separate the gallbladder from the liver, bile ducts, and other structures. Then the surgeon cuts the cystic duct and removes the gallbladder through one of the small incisions.</p>
<p>Recovery after laparoscopic surgery usually involves only one night in the hospital, and normal activity can be resumed after a few days at home. Because the abdominal muscles are not cut during laparoscopic surgery, patients have less pain and fewer complications than after “open” surgery, which requires a 5- to 8-inch incision across the abdomen.</p>
<p>If tests show the gallbladder has severe inflammation, infection, or scarring from other operations, the surgeon may perform open surgery to remove the gallbladder. In some cases, open surgery is planned; however, sometimes these problems are discovered during the laparoscopy and the surgeon must make a larger incision. Recovery from open surgery usually requires 3 to 5 days in the hospital and several weeks at home. Open surgery is necessary in about 5 percent of gallbladder operations.</p>
<p>The most common complication in gallbladder surgery is injury to the bile ducts. An injured common bile duct can leak bile and cause a painful and potentially dangerous infection. Mild injuries can sometimes be treated nonsurgically. Major injury, however, is more serious and requires additional surgery.</p>
<p>If gallstones are present in the bile ducts, the physician — usually a gastroenterologist — may use ERCP to locate and remove them before or during gallbladder surgery. Occasionally, a person who has had a cholecystectomy is diagnosed with a gallstone in the bile ducts weeks, months, or even years after the surgery. The ERCP procedure is usually successful in removing the stone in these cases.<br>
Because gallstone symptoms may be similar to those of a heart attack, appendicitis, ulcers, irritable bowel syndrome, hiatal hernia, pancreatitis, and hepatitis, an accurate diagnosis is important.</p>
<p><span style="text-decoration: underline;">Non-surgical Treatment</span></p>
<p>Nonsurgical approaches are used only in special situations — such as when a patient has a serious medical condition preventing surgery — and only for cholesterol stones. Stones commonly recur within 5 years in patients treated non-surgically.</p>
<p>Oral dissolution therapy. Drugs made from bile acid are used to dissolve gallstones. The drugs ursodiol (Actigall) and chenodiol (Chenix) work best for small cholesterol stones. Months or years of treatment may be necessary before all the stones dissolve. Both drugs may cause mild diarrhea, and chenodiol may temporarily raise levels of blood cholesterol and the liver enzyme transaminase.</p>
<p>Contact dissolution therapy. This experimental procedure involves injecting a drug directly into the gallbladder to dissolve cholesterol stones. The drug — methyl tert-butyl ether — can dissolve some stones in 1 to 3 days, but it causes irritation and some complications have been reported. The procedure is being tested in symptomatic patients with small stones.</p>
<p><strong>Do people need their gallbladder?</strong></p>
<p>Fortunately, the gallbladder is an organ people can live without. Your liver produces enough bile to digest a normal diet. Once the gallbladder is removed, bile flows out of the liver through the hepatic ducts into the common bile duct and directly into the small intestine, instead of being stored in the gallbladder. Because now the bile flows into the small intestine more often, softer and more frequent stools can occur in about 1 percent of people. These changes are usually temporary, but talk with your health care provider if they persist.</p>
<p><strong>Points to Remember</strong></p>
<ul  class="arrow-list">
<li>Gallstones form when bile hardens in the gallbladder.</li>
<li>Gallstones are more common among older adults; women; American Indians; Mexican Americans; people with diabetes; those with a family history of gallstones; people who are overweight, obese, or undergo rapid weight loss; and those taking cholesterol-lowering drugs.</li>
<li>Gallbladder attacks often occur after eating a meal, especially one high in fat.</li>
<li>Symptoms can mimic those of other problems, including a heart attack, so an accurate diagnosis is important.</li>
<li>Gallstones can cause serious problems if they become trapped in the bile ducts.</li>
<li>Laparoscopic surgery to remove the gallbladder is the most common treatment.</li>
</ul>
<p><strong>Hope through Research</strong></p>
<p>Animal research has helped identify several genes that may determine which people are more prone to gallstones. In spite of many well-defined risk factors for gallstones, researchers continue to look for genes that may identify susceptibility in humans. Researchers also believe that the growing global epidemic of obesity may increase rates of gallbladder disease worldwide.</p>
