				 <h3 id="wha">What is fecal incontinence?</h3>

<p>Fecal incontinence is the inability to control your bowels. When you feel the urge to have a bowel movement, you may not be able to hold it until you get to a toilet. Or stool may leak from the rectum unexpectedly, sometimes while passing gas.</p>
<p>More than 5.5 million Americans have fecal incontinence. It affects people of all ages — children and adults. Fecal incontinence is more common in women and older adults, but it is not a normal part of aging.</p>
<p>Loss of bowel control can be devastating. People who have fecal incontinence may feel ashamed, embarrassed, or humiliated. Some don’t want to leave the house out of fear they might have an accident in public. Most try to hide the problem as long as possible, so they withdraw from friends and family. The social isolation is unfortunate but may be reduced with treatment that improves bowel control and makes incontinence easier to manage.</p>
<p><strong>What causes fecal incontinence?</strong></p>
<p>Fecal incontinence can have several causes:</p>
<ul>
<li>constipation</li>
<li>damage to the anal sphincter muscles</li>
<li>damage to the nerves of the anal sphincter muscles or the rectum</li>
<li>loss of storage capacity in the rectum</li>
<li>diarrhea</li>
<li>pelvic floor dysfunction</li>
</ul>
<p><span style="text-decoration: underline;">Constipation</span></p>
<p>Constipation is one of the most common causes of fecal incontinence. Constipation causes large, hard stools to become lodged in the rectum. Watery stool can then leak out around the hardened stool. Constipation also causes the muscles of the rectum to stretch, which weakens the muscles so they can’t hold stool in the rectum long enough for a person to reach a bathroom.</p>
<p><span style="text-decoration: underline;">Muscle Damage</span></p>
<p>Fecal incontinence can be caused by injury to one or both of the ring-like muscles at the end of the rectum called the anal internal and external sphincters. The sphincters keep stool inside. When damaged, the muscles aren’t strong enough to do their job and stool can leak out. In women, the damage often happens when giving birth. The risk of injury is greatest if the doctor uses forceps to help deliver the baby or performs an episiotomy, which is a cut in the vaginal area to prevent it from tearing during birth. Hemorrhoid surgery can also damage the sphincters.</p>
<p><span style="text-decoration: underline;">Nerve Damage</span></p>
<p>Fecal incontinence can be caused by damage to the nerves that control the anal sphincters or the nerves that sense stool in the rectum. If the nerves that control the sphincters are injured, the muscles don’t work properly and incontinence can occur. If the sensory nerves are damaged, they don’t sense that stool is in the rectum so you won’t feel the need to use the bathroom until stool has leaked out. Nerve damage can be caused by childbirth, a long-term habit of straining to pass stool, stroke, physical disability due to injury, and diseases that affect the nerves such as diabetes and multiple sclerosis.</p>
<p><span style="text-decoration: underline;">Loss of Storage Capacity</span></p>
<p>Normally, the rectum stretches to hold stool until you can get to a bathroom. But rectal surgery, radiation treatment, and inflammatory bowel disease can cause scarring that makes the walls of the rectum stiff and less elastic. The rectum then can’t stretch as much to hold stool and fecal incontinence results. Inflammatory bowel disease also can irritate rectal walls, making them unable to contain stool.</p>
<p><span style="text-decoration: underline;">Diarrhea</span></p>
<p>Diarrhea, or loose stool, is more difficult to control than solid stool because with diarrhea the rectum fills with stool at a faster rate. Even people who don’t have fecal incontinence can leak stool when they have diarrhea.</p>
<p><span style="text-decoration: underline;">Pelvic Floor Dysfunction</span></p>
<p>Abnormalities of the pelvic floor muscles and nerves can cause fecal incontinence. Examples include:</p>
<ul>
<li>impaired ability to sense stool in the rectum</li>
<li>decreased ability to contract muscles in the anal canal to defecate</li>
<li>dropping down of the rectum, a condition called rectal prolapse</li>
<li>protrusion of the rectum through the vagina, a condition called rectocele</li>
<li>general weakness and sagging of the pelvic floor</li>
</ul>
<p>Childbirth is often the cause of pelvic floor dysfunction, and incontinence usually doesn’t appear until the mid-forties or later.</p>
<p><strong>How is fecal incontinence treated?</strong></p>
<p>Effective treatments are available for fecal incontinence and can improve or restore bowel control. The type of treatment depends on the cause and severity of fecal incontinence; it may include dietary changes, medication, bowel training, or surgery. More than one treatment may be necessary for successful control because continence is a complicated chain of events.</p>
<p><span style="text-decoration: underline;">Dietary Changes</span></p>
<p>Food affects the consistency of stool and how quickly it passes through the digestive system. If your stools are hard to control because they are watery, you may find that eating high-fiber foods adds bulk and makes stool easier to control. But people with well-formed stools may find that high-fiber foods act as a laxative and contribute to the problem. Foods and drinks that may make the problem worse are those containing caffeine — like coffee, tea, or chocolate — which relaxes the internal anal sphincter muscles.</p>
<p>You can adjust what and how you eat to help manage fecal incontinence.</p>
<ul>
<li><strong>Keep a food diary</strong> – List what you eat, how much you eat, and when you have an incontinent episode. After a few days, you may begin to see a pattern involving certain foods and incontinence. After you identify foods that seem to cause problems, cut back on them and see whether incontinence improves. Foods and drinks that typically cause diarrhea, and so should probably be avoided, include:
<ul>
<li>drinks and foods containing caffeine</li>
<li>cured or smoked meat such as sausage, ham, or turkey</li>
<li>spicy foods</li>
<li>alcoholic beverages</li>
<li>dairy products such as milk, cheese, or ice cream</li>
<li>fruits such as apples, peaches, or pears</li>
<li>fatty and greasy foods</li>
<li>sweeteners, such as sorbitol, xylitol, mannitol, and fructose, which are found in diet drinks, sugarless gum and candy, chocolate, and fruit juices</li>
</ul>
</li>
<li><strong>Eat small meals more frequently</strong> – In some people, large meals cause bowel contractions that lead to diarrhea. You can still eat the same amount of food in a day, but space it out by eating several small meals.</li>
<li><strong>Eat and drink at different times</strong> – Liquid helps move food through the digestive system. So if you want to slow things down, drink something half an hour before or after meals, but not with meals.</li>
<li><strong>Eat the right amount of fiber</strong> – For many people, fiber makes stool soft, formed, and easier to control. Fiber is found in fruits, vegetables, and grains, like those listed below. You need to eat 20 to 30 grams of fiber a day, but add it to your diet slowly so your body can adjust. Too much fiber all at once can cause bloating, gas, or even diarrhea. Also, too much insoluble, or indigestible, fiber can contribute to diarrhea. If you find that eating more fiber makes your diarrhea worse, try cutting back to two servings each of fruits and vegetables and removing skins and seeds from your food.</li>
<li><strong>Eat foods that make stool bulkier</strong> – Foods that contain soluble, or digestible, fiber slow the emptying of the bowels, including bananas, rice, tapioca, bread, potatoes, applesauce, cheese, smooth peanut butter, yogurt, pasta, and oatmeal.</li>
<li><strong>Get plenty to drink</strong> – Drink eight 8-ounce glasses of liquid a day to help prevent dehydration and keep stool soft and formed. Water is a good choice. Avoid drinks with caffeine, alcohol, milk, or carbonation if you find they trigger diarrhea.</li>
</ul>
<p>Over time, diarrhea can keep your body from absorbing vitamins and minerals. Ask your doctor if you need a vitamin supplement.</p>
