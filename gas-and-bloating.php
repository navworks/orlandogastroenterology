					Gas and Bloating 
				</h2>
				
				  <h4>Why do I have gas?</h4>
				  <p>Everyone has gas. Burping and “passing gas” is normal. But because it is embarrassing, many people believe they pass gas too often or have too much gas. A person actually having too much gas is rare.</p>
				<p>Most of the time, gas in the body is odorless. The odor of passed gas comes from sulfur made by bacteria in the large intestine. Sometimes gas causes bloating and pain. Not everyone has these symptoms. How much gas the body makes and how sensitive a person is to gas in the large intestine have an effect on how uncomfortable one feels.</p>
				<h4>What can I do about gas?</h4>
				<p>Changing what you eat and drink can help prevent or reduce gas. If you feel like you have too much gas, you might want to try these things before going to the doctor.</p>
				<p>1. Cut down on foods that cause gas.</p>
				<p>The amount of gas caused by certain foods varies from person to person. The only way to know your own limits is to keep track of what you eat and how much gas it causes later. Some foods that cause gas are:</p>
				<ul>
				<li>beans</li>
				<li>vegetables such as broccoli, cabbage, brussels sprouts, onions, artichokes, and asparagus</li>
				<li>fruits such as pears, apples, and peaches</li>
				<li>whole grains such as whole wheat and bran</li>
				<li>soft drinks and fruit drinks</li>
				<li>milk and milk products such as cheese and ice cream</li>
				<li>packaged foods that contain lactose, a type of sugar; bread, cereal, and salad dressing are examples</li>
				<li>dietetic foods and sugar-free candies and gums</li>
				</ul>
				<p>2. Drink plenty of water and clear soup but not “fizzy” liquids.</p>
				<p>Try not to drink liquids that cause gas, like soda and beer.</p>
				<p>3. Reduce the amount of air you swallow.</p>
				<p>Here are some ways to avoid swallowing air:</p>
				<ul>
				<li>Eat slower and chew more to cut down on the amount of air you swallow when you eat.</li>
				<li>Avoid chewing gum and eating hard candy.</li>
				<li>If you smoke, try to cut down or quit.</li>
				<li>If you have false teeth, see your dentist to make sure they fit right.</li>
				</ul>
				<p>4. Keep a diary.</p>
				<p>Write down the foods, and the amounts, that seem to cause you the most problems. Also keep track of the number of times you pass gas each day.</p>
				<p>If you are still troubled by gas, you may want to see your doctor. Take your diary with you to help you answer the doctor’s questions about eating habits and symptoms.</p>
				<h4>Points to Remember</h4>
				<ol>
				<li>Everyone has gas in the digestive tract.</li>
				<li>People often think they pass too much gas when they really don’t.</li>
				<li>Passing gas frequently is normal.</li>
				<li>Two ways to reduce the amount of gas you have are to:
				<ul>– cut down on the foods and liquids that cause gas</ul>
				<ul>– reduce the amount of air you swallow</ul>
				</li>
				</ol>
