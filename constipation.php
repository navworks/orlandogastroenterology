<p>Your doctor may suggest you take fiber pills or powder to help soften and bulk up the stool. You can buy fiber products in a pharmacy or grocery store without a prescription. Some fiber products are flavored while others are not. Be sure and take the fiber with plenty of water as directed.</p>
<p>Some people have gas and bloating at first when taking extra fiber. Let your doctor know if you are having problems that do not go away after a few days.</p>
<p><em><strong>2. Drink plenty of water and other liquids such as fruit and vegetable juices and clear soups.</strong></em></p>
<p>Liquids have little effect on stool form; however, drinking enough fluids is important because dehydration can cause constipation. Try not to drink liquids that contain caffeine or alcohol if you feel thirsty or dehydrated.</p>
<p><em><strong>3. Get enough exercise.</strong></em></p>
<p>Regular exercise helps your digestive system stay active and healthy. Exercising 20 to 30 minutes every day may help.</p>
<p><em><strong>4. Visit the restroom when you feel the urge to have a bowel movement. Allow yourself enough time to relax.</strong></em></p>
<p>Sometimes people feel so hurried that they don’t pay attention to their body’s needs. Make sure you visit the restroom when you feel the urge to have a bowel movement. If you usually have a bowel movement at a certain time of day, visit the restroom around that time. Reading a book or magazine in the restroom can help you relax. If you cannot have a bowel movement within 10 minutes, get up and return the next time you get the urge.</p>
<p><em><strong>5. Use laxatives only if a doctor says you should.</strong></em></p>
<p>Laxatives are medicines that help you pass stool. Most people who are mildly constipated do not need laxatives. However, if you are doing all the right things and you are still constipated, your doctor may recommend a laxative for a limited time. Your doctor will tell you what type is best for you. Laxatives come in many forms including liquid, chewing gum, and pills.</p>
<p><em><strong>6. Check with your doctor about any medicines you take.</strong></em></p>
<p>Some medicines can cause constipation. Be sure to ask your doctor if any medicines you are taking could cause constipation.</p>
<p><em><strong>7. Follow any special treatments your doctor recommends.</strong></em></p>
<p>If you have problems with the muscles and nerves that control bowel movements, your doctor may suggest biofeedback. Biofeedback is a painless process that uses sensors in the rectal area to help you feel the stool and move it out of the rectum. Doing biofeedback with a trained therapist has been shown to help some people with constipation.</p>
<p><strong>Points to Remember</strong></p>
<ul  class="arrow-list">
<li>Constipation may be present if you have three or fewer bowel movements in a week or if the stool is hard, dry, painful, or difficult to pass.</li>
<li>Constipation affects almost everyone at one time or another.</li>
<li>In most cases, following these simple steps will help prevent constipation:
<ul  class="arrow-list">
<li>Eat a variety of foods, especially vegetables, fruits, and grains.</li>
<li>Drink plenty of liquids.</li>
<li>Exercise regularly.</li>
</ul>
</li>
<li>Visit the restroom when you feel the urge to have a bowel movement.</li>
<li>Fiber pills and powders may help relieve constipation.</li>
<li>Most people with mild constipation do not need laxatives. However, your doctor may recommend a laxative for a limited time if you have constipation that does not improve.</li>
<li>Some medicines can cause constipation.</li>
</ul>
