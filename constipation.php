				 <h3 id="wha">What is constipation?</h3>



<p>Constipation means different things to different people. You may have constipation if you have three or fewer bowel movements in a week or if stool is hard, dry, painful, or difficult to pass. Some people with constipation lack energy and feel full or bloated.</p>
<p>Some people think they have constipation if they don’t have a bowel movement every day. However, bowel habits are different for everyone. The foods you eat, how much you exercise, and other factors can affect your bowel habits.</p>
<p>At one time or another, almost everyone gets constipated. In most cases, it lasts for only a short time and is not serious. When you understand what causes constipation, you can take steps to prevent it.</p>
<p><strong>What causes constipation?</strong></p>
<p>To understand what causes constipation, it helps to know how the large intestine works. The large intestine removes most of the water from stool and changes it to a solid waste. The large intestine then moves the stool through the rectum and anus as a bowel movement.</p>
<p>Constipation occurs when stool passes through the large intestine too slowly. When stool stays in the large intestine too long, the intestine removes too much water, and the stool becomes hard and dry.</p>
<p>Some lifestyle habits that may cause constipation include:</p>
<ul  class="arrow-list">
<li>changing your normal diet, exercise, or travel habits</li>
<li>ignoring the urge to have a bowel movement</li>
<li>feeling a lot of stress</li>
<li>eating a low-fiber diet</li>
<li>not drinking enough liquids</li>
<li>taking calcium or iron supplements</li>
<li>taking medicines such as painkillers with codeine; diuretics, also known as water pills; medicine for depression; and some antacids</li>
</ul>
<p>Some medical conditions that may cause constipation include:</p>
<ul  class="arrow-list">
<li>pregnancy or having given birth</li>
<li>problems with the muscles and nerves in the intestine, rectum, or anus</li>
<li>irritable bowel syndrome, a condition in which the nerves that control the muscles in the intestine don’t function correctly; the intestine becomes sensitive to food, stool, gas, and stress</li>
<li>diabetes, a condition in which a person has high blood sugar, also called hyperglycemia, because the body cannot use blood glucose, or blood sugar, for energy</li>
<li>hypothyroidism, a condition in which the thyroid gland does not produce enough hormone to meet the body’s needs and many of the body’s functions slow down</li>
</ul>
<p><strong>What tests are done to find the causes of constipation?</strong></p>
<p>To find out why you have constipation, your doctor will perform a complete physical examination. The doctor may also order one or more tests if a serious problem is suspected as the cause of constipation.</p>
<ul  class="arrow-list">
<li><strong>Sigmoidoscopy</strong> – The doctor puts a thin, flexible tube called a sigmoidoscope into the rectum. This scope can show the last third of your large intestine.</li>
<li><strong>Colonoscopy</strong> – The doctor looks at the entire large intestine with a long, flexible tube with a camera that shows images on a TV screen. The tube is like a sigmoidoscope but longer. You receive medicine to help you sleep during a colonoscopy.</li>
<li><strong>Colorectal transit study</strong> – For this test, you swallow small capsules that can be seen on an x-ray as they move through the large intestine and anus.</li>
<li><strong>Anorectal function test</strong> – The doctor inserts a small balloon into the anus to see if you are able to push it out.</li>
<li><strong>Defecography test</strong> – The doctor inserts a soft paste into the rectum. The doctor asks you to push out the paste while an x-ray machine takes pictures of the rectum and anus.</li>
</ul>
<p><strong>What can I do about constipation?</strong></p>
<p>You can take several steps to prevent and relieve constipation.</p>
<p><em><strong>1. Eat more fiber.</strong></em></p>
<p>Fiber helps form soft, bulky stools and is found in many vegetables, fruits, and grains. Be sure to add fiber to your diet a little at a time so your body gets used to it. Limit foods that have little or no fiber such as pizza, ice cream, cheese, meat, snacks like chips, and processed foods such as instant mashed potatoes or frozen dinners.</p>
